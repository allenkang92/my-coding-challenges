# 문제 링크 : https://school.programmers.co.kr/learn/courses/30/lessons/120830?language=python3
# 간단한 문제 설명 : 
#   머쓱이네 양꼬치 가게는 10인분을 주문하면 음료수 하나를 서비스로 제공합니다.
#   양꼬치 1인분의 가격은 12,000원이며, 음료수 1개당 가격은 2,000원입니다.
#   정수 n과 k가 주어졌을 때, 
#   양꼬치 n인분과 음료수 k개를 주문한다면 총 얼마를 지불해야 하는지를 계산합니다.
#
# 해결 방법 설명 :
#   - 기본 금액은 (n * 12000) + (k * 2000)
#   - 10인분 주문마다 음료수 1개가 서비스로 제공되어, 서비스 음료수에 해당하는
#     금액 (n // 10 * 2000)을 차감합니다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 단순 산술 연산만 수행
#   - 공간 복잡도: O(1) - 상수 공간만 사용

def solution(n, k):
    answer = 0
    service = 0
    # 10인분 주문 시 서비스 음료수 개수 계산
    if n // 10 >= 1 and k > 0:
        service = (n // 10)
    if service < 0:
        service = 0
    answer = (n * 12000) + (k * 2000) - (service * 2000)
    return answer