# 문제 링크 (주석) : https://school.programmers.co.kr/learn/courses/30/lessons/120853
# 간단한 문제 설명 : 
#   공백으로 구분된 숫자와 "Z"로 이루어진 문자열이 주어질 때,
#   숫자들을 순서대로 더하되 "Z"가 나오면 바로 전에 더했던 숫자를 빼는 문제입니다.
#
# 해결 방법 설명 : 
#   - 스택을 사용하여 숫자들을 저장합니다.
#   - 문자열을 공백으로 분리하여 각 원소를 순회합니다:
#     1. 현재 원소가 "Z"이면 스택에서 마지막 숫자를 제거(pop)합니다.
#     2. 숫자이면 정수로 변환하여 스택에 추가(append)합니다.
#   - 마지막에 스택에 남아있는 모든 숫자의 합을 반환합니다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(n) (n은 입력 문자열의 원소 개수)
#   - 공간 복잡도: O(n) (스택에 저장되는 숫자의 최대 개수)

def solution(s):
    stack = []
    for i in s.split():
        if i == "Z":
            if stack:  # 스택이 비어있지 않은 경우
                stack.pop()  # 마지막에 추가된 숫자 제거
        else:
            stack.append(int(i))
    return sum(stack)