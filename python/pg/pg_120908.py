# 문제 링크 : (문제 링크 없음)
# 간단한 문제 설명 : 
#   문자열 str1, str2가 주어질 때,
#   str1 안에 str2가 있으면 1을, 없으면 2를 반환하는 문제입니다.
#
# 해결 방법 설명 :
#   - in 연산자를 사용하여 str2가 str1에 포함되어 있는지 확인합니다.
#
# 시간/공간 복잡도 :
#   - 시간 복잡도: O(n) - str1 검색에 소요되는 시간
#   - 공간 복잡도: O(1) - 추가 공간 사용 없음

def solution(str1, str2):
    if str2 in str1:
        return 1
    else:
        return 2