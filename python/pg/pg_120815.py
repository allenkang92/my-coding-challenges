# 문제 링크 (주석) : https://school.programmers.co.kr/learn/courses/30/lessons/120815
# 간단한 문제 설명 :
#   피자를 여섯 조각으로 자른 후, n명이 남김없이 모두 같은 수의 조각을 먹기 위해 필요한 최소 피자 판 수를 구하는 문제.
#
# 해결 방법 설명 :
#   - 한 판의 피자는 6조각이다.
#   - n명이 모두 같은 양을 먹으려면, 피자 수(판 수)를 i라 할 때 총 조각 수는 6 * i가 된다.
#   - 이때, 6 * i가 n으로 나누어 떨어지면, 각 사람은 (6 * i) / n 조각씩 먹을 수 있으며, 남는 조각이 없다.
#   - 최소 i를 찾기 위해 1부터 순회하며 (6 * i) % n == 0인 경우를 찾는다.
#
# 시간/공간 복잡도 :
#   - 시간 복잡도: O(100) → 상수 (최대 n은 100이므로, 1부터 100까지 순회)
#   - 공간 복잡도: O(1) → 상수 공간 사용

def solution(n):
    # 1부터 100까지 순회하며 조건을 만족하는 최소 피자 판 수를 찾는다.
    for i in range(1, 101):
        # (한 판당 6조각 * i판) 총 조각 수가 n으로 나누어 떨어지는지 확인.
        if (6 * i) % n == 0:
            return i