# 문제 링크 : https://school.programmers.co.kr/learn/courses/30/lessons/120819
# 간단한 문제 설명 :
#   머쓱이가 가진 돈 money로 얼만큼의 아이스 아메리카노(1잔=5,500원)를 구매할 수 있는지,
#   그리고 남는 잔돈은 얼마인지를 구하는 문제.
#
# 해결 방법 설명 :
#   - money를 5500으로 나눈 몫은 구매 가능한 아이스 아메리카노의 잔 수이다.
#   - money를 5500으로 나눈 나머지는 남는 잔돈이다.
#   - 결과를 [잔 수, 남은 돈]의 형태로 배열로 반환한다.
#
# 시간/공간 복잡도 :
#   - 시간 복잡도: O(1) - 단순 산술 연산
#   - 공간 복잡도: O(1) - 상수 공간 사용

def solution(money):
    # 아이스 아메리카노 한 잔의 가격은 5500원
    cups = money // 5500    # 구매할 수 있는 잔 수
    changes = money % 5500  # 남는 잔돈
    return [cups, changes]