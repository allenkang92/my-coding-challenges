# 문제 링크 (주석) : https://school.programmers.co.kr/learn/courses/30/lessons/120838
# 간단한 문제 설명 : 
#   공백으로 구분된 모스부호 문자열을 영어 소문자로 해독하는 문제입니다.
#
# 해결 방법 설명 : 
#   - 모스부호와 알파벳의 매핑을 딕셔너리로 저장합니다.
#   - 입력된 문자열을 공백으로 분리하여 각 모스부호를 얻습니다.
#   - 각 모스부호를 딕셔너리를 통해 알파벳으로 변환합니다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(n) (n은 모스부호의 개수)
#   - 공간 복잡도: O(1) (고정된 크기의 딕셔너리 사용)

def solution(letter):
    answer = ''
    morse = { 
    '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
    '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
    '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
    '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
    '-.--':'y','--..':'z'
    }
    morse_trans = letter.split()
    for i in morse_trans:
        answer += morse[i]
    return answer