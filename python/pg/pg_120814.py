# 문제 링크 (주석) : https://school.programmers.co.kr/learn/courses/30/lessons/120814
# 간단한 문제 설명 : 
#   피자를 일곱 조각으로 자른다.
#   피자를 나눠 먹을 사람의 수 n이 주어졌을 때,
#   모든 사람이 한 조각 이상 먹기 위해 필요한 최소 피자 판 수를 구한다.
#
# 해결 방법 설명 : 
#   - 한 판의 피자는 7조각이다.
#   - n명이 한 조각씩 먹으려면 n조각 필요하다.
#   - n조각을 7조각으로 나눠서 올림하면 필요한 피자 판 수가 된다.
#     math.ceil(n / 7)을 사용하거나, (n + 6) // 7 연산을 사용할 수 있다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 단순 산술 연산만 수행
#   - 공간 복잡도: O(1) - 상수 공간 사용

import math

def solution(n):
    # n명의 사람에게 피자 한 조각씩 주기 위해 필요한 피자 판 수
    return math.ceil(n / 7)
