# 문제 링크 : https://school.programmers.co.kr/learn/courses/30/lessons/120922
# 간단한 문제 설명 : 
#   M x N 크기의 종이를 1 x 1 크기로 자르기 위한 최소 가위질 횟수를 구합니다.
#
# 해결 방법 설명 : 
#   - 가로로 M-1번, 세로로 N-1번의 가위질이 필요합니다.
#   - 가로와 세로의 가위질을 더하면 총 (M-1) + (N-1)번 = M + N - 2번
#   - 각 조각마다 가위질이 필요하므로 이를 곱하면 M * N - 1번이 됩니다.
#   - 단, 1 x 1 크기는 자를 필요가 없으므로 0을 반환합니다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1)
#   - 공간 복잡도: O(1)

def solution(M, N):
    if M * N == 1:  # 1 x 1 크기는 자를 필요가 없음
        return 0
    return M * N - 1  # 총 가위질 횟수