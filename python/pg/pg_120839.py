# 문제 설명:
#   가위 바위 보에서, "2"는 가위, "0"은 바위, "5"는 보를 나타냅니다.
#   rsp에 저장된 가위 바위 보에 대해, 모두 이기는 경우의 가위 바위 보를
#   순서대로 나타낸 문자열을 반환합니다.
#   예를 들어, rsp가 "205"라면, 순서대로 가위(2), 바위(0), 보(5)에 대한
#   이기는 경우는 바위(0), 보(5), 가위(2)이므로 "052"를 반환해야 합니다.
#
# 해결 방법 설명 :
#   - rsp 문자열의 각 문자를 순회하며 해당 문자에 대응하는 이기는 문자를 result에 누적합니다.
#
# 시간/공간 복잡도 :
#   - 시간 복잡도: O(n) (n은 rsp 문자열의 길이)
#   - 공간 복잡도: O(n) (결과 문자열 저장)

def solution(rsp):
    answer = ''
    for ch in rsp:
        if ch == "2":       # 가위(2)에 이기는 것은 바위(0)
            answer += "0"
        elif ch == "0":     # 바위(0)에 이기는 것은 보(5)
            answer += "5"
        elif ch == "5":     # 보(5)에 이기는 것은 가위(2)
            answer += "2"
    return answer