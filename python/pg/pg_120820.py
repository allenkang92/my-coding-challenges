# 문제 링크 : https://school.programmers.co.kr/learn/courses/30/lessons/120820
# 간단한 문제 설명 : 
#   2022년 기준 선생님의 나이 age가 주어질 때, 
#   선생님의 출생 연도를 반환하는 문제.
#   (출생 연도 계산: 태어난 해에 1살이며, 매년 1월 1일마다 1살씩 증가)
#
# 해결 방법 설명 :
#   - 2022년에서 나이를 빼고 1을 더하면 출생 연도를 구할 수 있다.
#     즉, 출생 연도 = 2022 - age + 1
#
# 시간/공간 복잡도 :
#   - 시간 복잡도: O(1) - 단순 산술 연산
#   - 공간 복잡도: O(1) - 상수 공간만 사용

def solution(age):
    answer = 2022 - age + 1
    return answer