# 문제 링크 (주석) : https://www.acmicpc.net/problem/2753
# 간단한 문제 설명 : 
#   주어진 연도가 윤년이면 1, 아니면 0을 출력하는 프로그램을 작성한다.
#   윤년의 조건은 다음과 같다:
#     - 연도가 4의 배수이고 100의 배수가 아닐 때 또는
#     - 연도가 400의 배수일 때
#
# 해결 방법 설명 : 
#   - 입력으로 받은 연도를 정수형으로 변환한다.
#   - 위의 윤년 조건을 if-elif-else 문을 사용하여 판별한다.
#   - 조건에 맞게 1 또는 0을 출력한다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 상수 시간에 실행됨
#   - 공간 복잡도: O(1) - 상수 공간을 사용함

year = int(input())

if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
    print(1)
else:
    print(0)