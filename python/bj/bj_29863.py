# 문제 링크 (주석) : https://www.acmicpc.net/problem/29863
# 문제 설명:
#   아르노는 정각(24시간제)으로 주어진 취침 시간에서 알람 시간이 울리기 전까지 몇 시간 동안 잘 수 있는지 계산하는 문제입니다.
#   취침 시간 S는 20~23시 또는 0~3시로 주어지고, 알람 시간 W는 5~10시로 주어집니다.
#   취침 시간이 20시 이상인 경우, 자정까지 남은 시간(24 - S)와 알람 시간 W를 더한 값이 총 수면 시간입니다.
#   취침 시간이 0시 이상 3시 이하인 경우, 단순히 알람 시간에서 취침 시간을 뺀 값이 수면 시간입니다.
#
# 해결 방법 설명:
#   주어진 조건에 따라 두 가지 경우로 나누어 계산합니다.
#
# 시간/공간 복잡도: O(1)

S = int(input())
W = int(input())

if 20 <= S <= 23:
    # 취침 시간이 20시 이상일 경우: 자정까지의 시간 + 알람 시간
    print((24 - S) + W)
elif 0 <= S <= 3:
    # 취침 시간이 0 ~ 3시일 경우: 알람 시간 - 취침 시간
    print(W - S)