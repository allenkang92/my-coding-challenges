# 문제 링크 : https://www.acmicpc.net/problem/30045
# 문제 설명:
#   ZOAC 6의 오프닝 홍보 글에서 각 문장이 "01" 또는 "OI"를 포함하면 문장 끝에 영일이 이모티콘을 추가한다.
#   총 몇 번의 이모티콘이 추가되는지 구하는 문제이다.
#
# 입력:
#   첫 번째 줄에 홍보 글의 문장 수 T가 주어진다. (1 ≤ T ≤ 500)
#   이후 T개의 줄에 걸쳐 문장 S가 주어진다.
#   각 문장 S는 공백 없이 영문 알파벳과 숫자로만 이루어진다. (1 ≤ |S| ≤ 500)
#
# 출력:
#   홍보 글에 영일이 이모티콘을 추가한 횟수를 출력한다.
#
# 해결 방법:
#   각 문장에서 "01" 또는 "OI"가 포함되어 있으면 이모티콘을 추가하는 것으로 간주하고, 
#   그 경우의 수를 센다.
#
# 시간/공간 복잡도: O(T * |S|)

T = int(input())
count = 0

for _ in range(T):
    s = input().strip()
    if "01" in s or "OI" in s:
        count += 1

print(count)