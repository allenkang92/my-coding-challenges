# 문제 링크 (주석) : https://www.acmicpc.net/problem/25238
# 문제 설명:
#   메이플스토리 몬스터의 방어율 a와 유저의 방무(b, %)가 주어질 때,
#   유저가 체감하는 몬스터의 방어율은 a - (a × (b / 100))로 계산됩니다.
#   이 체감 방어율이 100 이상이면 유저는 몬스터에게 데미지를 줄 수 없고(출력 0),
#   100 미만이면 데미지를 줄 수 있습니다(출력 1).
#
# 해결 방법:
#   a와 b를 입력받아, 체감 방어율을 계산하고 조건에 따라 0 또는 1을 출력합니다.
#
# 시간/공간 복잡도 : O(1)

a, b = map(int, input().split(" "))

effective_defense = a - (a * (b / 100))

if effective_defense >= 100:
    print(0)
else:
    print(1)