# 문제 링크: https://www.acmicpc.net/problem/16486
# 문제 설명:
#   세연이네 학교 운동장은 두 개의 반원과 하나의 직사각형으로 구성되어 있습니다.
#   직사각형의 가로 길이를 d1, 반원의 반지름을 d2라고 할 때,
#   운동장의 한 바퀴 둘레는 직사각형의 양쪽 가로 길이와 두 반원을 합친 원의 둘레로 계산됩니다.
#   두 반원은 합치면 한 원의 둘레와 같으므로, 원의 둘레는 2 * π * d2 입니다.
#   따라서 한 바퀴 둘레는 2 * d1 + 2 * π * d2 로 구할 수 있습니다.
#
# 해결 방법 설명:
#   1. d1과 d2의 값을 입력받습니다.
#   2. 직사각형 부분의 둘레 = d1 * 2.
#   3. 두 반원을 합친 원의 둘레 = d2 * 3.141592 * 2 (문제에서 π의 값은 3.141592로 사용).
#   4. 두 값을 더하여 총 둘레를 출력합니다.
#
# 시간/공간 복잡도: O(1)

d1 = int(input())
d2 = int(input())

rec = d1 * 2
round_part = d2 * 3.141592 * 2  # 반원 두 개를 합친 원의 둘레

print(rec + round_part)