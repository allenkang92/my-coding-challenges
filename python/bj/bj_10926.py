# 간단한 문제 설명 :
#   준하는 회원가입을 시도했으나 이미 존재하는 아이디를 발견하고 놀람을 표현하기 위해 아이디 뒤에 "??!"를 붙여 출력해야 합니다.
#
# 해결 방법 설명 :
#   1. 표준 입력으로 주어진 기존 아이디를 변수에 저장합니다.
#   2. 저장된 아이디 문자열 뒤에 "??!"를 연결하여 새로운 문자열을 생성합니다.
#   3. 생성된 문자열을 출력합니다.
#
# 시간/공간 복잡도 :
#   - 시간 복잡도 : O(1)
#       - 입력받는 문자열의 길이에 상관없이 단순히 문자열을 연결하고 출력하는 작업이므로 상수 시간 내에 처리됩니다.
#   - 공간 복잡도 : O(1)
#       - 추가적인 저장 공간이 입력 문자열의 길이에 비례하지 않고, 단순히 연결된 결과 문자열을 저장하기 때문입니다.



# 사용자로부터 기존 아이디 입력 받기
