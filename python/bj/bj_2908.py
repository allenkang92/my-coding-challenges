# 문제 링크 (주석) : https://www.acmicpc.net/problem/2908
# 간단한 문제 설명 : 
#   세 자리 수 두 개를 거꾸로 읽어서 더 큰 수를 출력
#   예: 734 893 -> 437 398 중 큰 수인 437 출력
#
# 해결 방법 설명 : 
#   - 두 수를 입력받아 공백으로 분리
#   - 각 수를 문자열 슬라이싱으로 뒤집기 ([::-1])
#   - 뒤집은 수를 정수로 변환하여 비교
#   - 더 큰 수를 출력
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 항상 세 자리 수만 처리
#   - 공간 복잡도: O(1) - 상수 공간만 사용

A, B = input().split(" ")

A = int(A[::-1])
B = int(B[::-1])

answer = 0

if A > B:
    answer += A
else: 
    answer += B

print(answer)