# 문제 링크 (주석) : https://www.acmicpc.net/problem/26575
# 문제 설명:
#   강아지들을 입양한 것을 축하합니다! 이제 강아지들이 먹을 사료를 계산해서 얼마의 돈이 필요한지 알아봐야 합니다.
#   수의사로부터 각 강아지가 다음 쇼핑 때까지 먹을 사료의 파운드 수를 듣고,
#   강아지의 수와 강아지 당 사료 파운드 수, 사료의 파운드당 가격이 주어지면,
#   필요한 총 사료 비용을 계산하는 프로그램을 작성합니다.
#
# 입력:
#   첫 번째 줄에는 데이터 세트의 수 n이 주어집니다.
#   다음 n개의 줄 각각에는 강아지 수 d, 강아지 당 사료 소비량 f (파운드), 그리고 사료의 파운드당 가격 p가 주어집니다.
#
# 출력:
#   각 데이터 세트에 대하여, 사료 비용을 달러 기호와 함께 소수점 둘째 자리까지 반올림하여 출력합니다.
#
# 해결 방법:
#   각 데이터 세트마다 (강아지 수 × 사료 소비량 × 파운드당 가격)을 계산하여 출력합니다.
#
# 시간/공간 복잡도: O(n)

n = int(input())
for _ in range(n):
    d, f, p = map(float, input().split())
    print(f"${(d * f * p):.2f}")