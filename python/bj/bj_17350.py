# 문제 링크 : https://www.acmicpc.net/problem/17350
# 간단한 문제 설명 : 야구팀 선수 리스트에서 이름이 'anj'(뭐)인 선수가 있는지 확인하는 문제
# 해결 방법 설명 : 선수 이름을 리스트에 저장하고 'anj'가 있는지 확인하는 간단한 문자열 검색 방식 사용
# 시간/공간 복잡도 : O(N), N은 야구팀 멤버 수

# 야구팀 멤버 수 입력 받기
N = int(input())

# 선수 이름을 저장할 빈 리스트 생성
member = list()

# N명의 선수 이름을 입력받아 리스트에 추가
# 각 입력은 영어 소문자와 특정 대문자로 이루어진 선수 이름
for m in range(N):
    member.append(input())  # 각 선수 이름을 리스트에 추가

# 'anj'(뭐)라는 이름의 선수가 리스트에 있는지 검사
if 'anj' in member:
    print("뭐야;")  # 있으면 "뭐야;"를 출력
else:
    print("뭐야?")  # 없으면 "뭐야?"를 출력