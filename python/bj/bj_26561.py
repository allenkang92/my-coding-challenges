# 문제 링크 (주석) : https://www.acmicpc.net/problem/26561
# 문제 설명:
#   Emily의 나라에서는 7초에 한 명씩 사망하고, 4초에 한 명씩 탄생한다고 추정됩니다.
#   시작 인구 p와 경과 시간 t가 주어지면, t초가 지난 후의 추정 인구수를 구하는 문제입니다.
#
# 해결 방법:
#   t초 동안 탄생한 사람의 수는 t를 4로 나눈 몫(t // 4)이고,
#   t초 동안 사망한 사람의 수는 t를 7로 나눈 몫(t // 7)입니다.
#   따라서 최종 인구수는 p + (t // 4) - (t // 7)로 계산됩니다.
#
# 시간/공간 복잡도 : O(n)

n = int(input())
for _ in range(n):
    p, t = map(int, input().split(" "))
    count = p + (t // 4) - (t // 7)
    print(count)