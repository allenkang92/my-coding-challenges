# 문제 링크 : https://www.acmicpc.net/problem/14681
# 간단한 문제 설명 : 
#   주어진 점의 좌표에 따라 어느 사분면에 속하는지 판별하는 프로그램을 작성한다.
#   사분면은 1부터 4까지 번호로 구분하며, 
#     - 1사분면: x > 0, y > 0
#     - 2사분면: x < 0, y > 0
#     - 3사분면: x < 0, y < 0
#     - 4사분면: x > 0, y < 0
#
# 해결 방법 설명 : 
#   - 입력으로 정수 x와 y를 각각 받는다.
#   - x와 y의 부호를 비교하여 사분면을 결정한다.
#   - 조건문(if-elif-else)을 사용하여 각 사분면을 출력한다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 상수 시간에 실행됨
#   - 공간 복잡도: O(1) - 상수 공간을 사용함

x = int(input())
y = int(input())

if x > 0 and y > 0:
    print(1)
elif x < 0 and y > 0:
    print(2)
elif x < 0 and y < 0:
    print(3)
else:
    print(4)