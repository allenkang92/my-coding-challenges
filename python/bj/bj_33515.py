# 문제 링크 (주석) : https://www.acmicpc.net/problem/33515
# 문제 설명:
#   세 명 중 백준 1001번 문제를 가장 먼저 풀면 그 사람의 노트북을 사용한다.
#   세 사람 중 형진이를 제외한 두 팀원이 백준 1001번 문제를 푸는데 걸린 시간이 주어진다.
#   형진이가 자신의 노트북으로 대회에 참가하기 위해서는 다른 팀원들보다 
#   같거나 더 빠른 시간 내에 문제를 풀어야 한다.
#   따라서 형진이가 문제를 푸는데 걸릴 수 있는 최대 시간은 두 팀원 중
#   더 빠른(작은 시간) 값과 같다.
#
# 입력:
#   첫 번째 줄에 두 정수 T1, T2가 공백으로 구분되어 주어진다. 
#   (1 ≤ T1, T2 ≤ 10,000)
#
# 출력:
#   형진이가 자신의 노트북으로 대회에 참가하기 위해 문제를 풀어야 하는
#   최대 시간을 초 단위로 출력한다.
#
# 해결 방법:
#   두 팀원의 시간 중 최솟값을 출력한다.
#
# 시간/공간 복잡도: O(1)

T1, T2 = map(int, input().split())
print(min(T1, T2))