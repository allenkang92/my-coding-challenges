# 문제 링크 : https://www.acmicpc.net/problem/26742
# 문제 설명:
#   Bajtazar는 Bajtocji에서 유행하는 패션 트렌드에 맞게 단색 양말만을 착용하려고 합니다.
#   입력으로 주어지는 문자열에는 'B'와 'C' 문자가 등장하며,
#   'B'는 흰색 양말, 'C'는 검은색 양말을 나타냅니다.
#   Bajtazar는 양말들 중 단색인(흰색 또는 검은색) 양말로 이루어진 한 쌍을 만들고자 합니다.
#   한 쌍은 동일한 색의 양말 두 개를 의미하므로, 각 색별로 양말의 개수를 2로 나눈 몫을 구한 후 합산하면
#   만들 수 있는 단색 양말 쌍의 총 개수가 됩니다.
#
# 해결 방법:
#   입력 문자열에서 'B'와 'C'의 등장 횟수를 각각 세고, 각 횟수를 2로 나눈 몫을 구한 후 합산하여 출력합니다.
#
# 시간/공간 복잡도: O(n), n은 입력 문자열의 길이

line = input()
print((line.count("B") // 2) + (line.count("C") // 2))