# 문제 링크 : https://www.acmicpc.net/problem/26766
# 간단한 문제 설명 : 
#   N개의 ASCII 아트 하트를 세로로 출력하는 문제입니다.
#
# 해결 방법 설명 :            
#   1. 출력할 하트의 개수 N을 입력받습니다.
#   2. 반복문을 사용하여 N번 하트 모양의 ASCII 아트를 출력합니다.
#   3. 각 하트는 9줄로 구성되어 있으며, @ 문자를 사용하여 그립니다.
#
# 시간/공간 복잡도 : O(N) (N은 출력할 하트의 개수)

N = int(input())  # 출력할 하트의 개수 입력

# N번 반복하여 하트 모양 출력
for _ in range(N):
    print(''' @@@   @@@ 
@   @ @   @
@    @    @
@         @
 @       @ 
  @     @  
   @   @   
    @ @    
     @     ''')