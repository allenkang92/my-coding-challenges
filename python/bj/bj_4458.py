# 문제 링크 (주석) : https://www.acmicpc.net/problem/4458
# 문제 설명:
#   입력으로 주어지는 여러 문장에서 각 줄의 첫 번째 글자를 대문자로 변경하여 출력한다.
#
# 입력:
#   첫째 줄에 문장의 수 N이 주어진다.
#   다음 N개의 줄에는 문장이 주어진다.
#   모든 줄의 첫 번째 글자는 알파벳이다.
#
# 출력:
#   각 줄의 첫 글자만 대문자로 바꿔 출력한다.
#
# 해결 방법:
#   각 문장의 첫 번째 글자를 S[0].upper()로 변환하고, 나머지 부분 S[1:]와 합쳐 결과를 출력한다.
#
# 시간/공간 복잡도: O(N)

N = int(input())
for _ in range(N):
    S = input()
    # 첫 글자를 대문자로 바꾸어서 출력
    print(S[0].upper() + S[1:])