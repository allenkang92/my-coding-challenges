# 문제 링크 : https://www.acmicpc.net/problem/22155
# 문제 설명:
#   Russian Code Cup의 "Простая задача" 문제에서는, 각 문제의 해결에 사용된
#   분기(조건문) 연산자 개수 i와 반복(루프) 연산자 개수 f가 주어진다.
#   문제에서 선언된 간단한 문제의 조건은 다음과 같다:
#       - 조건 1: 분기 연산자가 1개 이하이고 반복 연산자가 2개 이하인 경우, 또는
#       - 조건 2: 분기 연산자가 2개 이하이고 반복 연산자가 1개 이하인 경우
#   두 조건 중 하나를 만족하면 문제는 간단한 것으로 간주되어 "Yes"를,
#   그렇지 않으면 "No"를 출력합니다.
#
# 해결 방법:
#   n개의 문제에 대해 각 문제마다 i와 f를 입력받아 위 조건에 따라 "Yes" 또는 "No"를 출력합니다.
#
# 시간/공간 복잡도 : O(n)

n = int(input())

for _ in range(n):
    i, f = map(int, input().split(" "))
    if (i <= 1 and f <= 2) or (i <= 2 and f <= 1):
        print("Yes")
    else:
        print("No")