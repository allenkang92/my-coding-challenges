# 문제 링크 : https://www.acmicpc.net/problem/31746
# 문제 설명:
#   민규가 가장 좋아하는 문자열 "SciComLove"를 N번 뒤집은 결과를 출력하는 문제입니다.
#   문자열을 뒤집는 것은 문자열의 순서를 거꾸로 만드는 것을 의미합니다.
#   한 번 뒤집으면 문자열이 뒤집히고, 두 번 뒤집으면 원래 문자열이 됩니다.
#   따라서 N이 짝수면 원래 문자열을, 홀수면 뒤집힌 문자열을 출력하면 됩니다.
#
# 입력:
#   첫 번째 줄에 정수 N (1 ≤ N ≤ 10^9)이 주어집니다.
#
# 출력:
#   "SciComLove"를 N번 뒤집은 결과를 출력합니다. (따옴표 제외)
#
# 시간/공간 복잡도: O(1)

N = int(input())
letter = "SciComLove"
if N % 2 == 0:
    print(letter)
else:
    print(letter[::-1])