# 문제 링크 : https://www.acmicpc.net/problem/27541
# 문제 설명:
#   길이 N의 문자열 S가 주어진다.
#   만약 S의 마지막 문자가 'G'라면, 마지막 문자 'G'를 제거한 문자열을 출력하고,
#   그렇지 않다면 S의 마지막에 문자 'G'를 추가한 문자열을 출력한다.
#
# 입력:
#   첫 번째 줄에는 정수 N (2 ≤ N ≤ 100)이 주어진다.
#   두 번째 줄에는 길이 N의 영어 대문자로 이루어진 문자열 S가 주어진다.
#
# 출력:
#   조건에 맞게 변형된 문자열을 출력한다.
#
# 해결 방법:
#   문자열 S의 마지막 문자를 확인한 후, 조건에 따라 처리한다.
#
# 시간/공간 복잡도 : O(N)

N = int(input())
S = input()

if S[-1] == "G":
    # 문자열의 마지막 문자가 'G'인 경우, 'G'를 제거한다.
    print(S[:-1])
else:
    # 문자열의 마지막 문자가 'G'가 아닌 경우, 끝에 'G'를 추가한다.
    print(S + "G")