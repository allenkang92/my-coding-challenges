# 문제 링크 (주석) : 문제 설명 참조
# 문제 설명:
#   임스는 매일 Rust 프로그래밍 언어 책을 공부하기 위해 
#   정해진 페이지 수 이상의 공부를 계획하였다.
#   실제 공부한 페이지 수와 계획한 페이지 수가 주어질 때,
#   임스가 계획한 페이지 수 이상으로 공부한 횟수를 구하는 문제이다.
#
# 입력:
#   첫 번째 줄에는 공부한 일수 N이 주어진다. (1 ≤ N ≤ 1000)
#   두 번째 줄에는 계획한 페이지 수 A_1, A_2, ..., A_N이 공백으로 구분되어 주어진다. (1 ≤ A_i ≤ 100)
#   세 번째 줄에는 실제 공부한 페이지 수 B_1, B_2, ..., B_N이 공백으로 구분되어 주어진다. (1 ≤ B_i ≤ 100)
#
# 출력:
#   임스가 계획한 페이지 수 이상으로 공부한 횟수를 출력한다.
#
# 해결 방법:
#   각 일자에 대해 실제 공부한 페이지 수가 계획한 페이지 수보다 크거나 같으면 카운팅한다.
#
# 시간/공간 복잡도: O(N)

N = int(input())
planned_pages = list(map(int, input().split()))
actual_pages = list(map(int, input().split()))

count = 0
for i in range(N):
    if actual_pages[i] >= planned_pages[i]:
        count += 1

print(count)