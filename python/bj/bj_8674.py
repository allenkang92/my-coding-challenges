# 문제 링크 (주석) : https://www.acmicpc.net/problem/8674
# 문제 설명:
#   엄마가 피오트루시오와 파벨코에게 가로 a, 세로 b의 초콜릿 한 판을 가져왔다.
#   두 아이는 초콜릿을 세로 또는 가로 방향으로 한 번만 자르려고 한다.
#   이때, 자른 후 양쪽 조각에 포함된 초콜릿 조각의 개수 차이가 최소가 되어야 한다.
#
# 해결 방법 설명:
#   a 또는 b 중 하나라도 짝수이면, 초콜릿을 정확히 동일한 두 조각으로 나눌 수 있으므로 차이는 0이다.
#   만약 a와 b 모두 홀수라면, 초콜릿 조각 수의 총합이 홀수가 되어 한 조각이 다른 조각보다
#   min(a, b)만큼 많게 된다. 따라서 최소 차이는 min(a, b)이다.
#
# 시간/공간 복잡도 : O(1)

a, b = map(int, input().split(" "))

if a % 2 == 0 or b % 2 == 0:
    print(0)
else:
    print(min(a, b))