# 문제 링크 : https://www.acmicpc.net/problem/30156
# 문제 설명:
#   Malvika는 풍선의 색깔에 대해 특이한 취향을 가지고 있다.
#   풍선 문자열 s는 'a'와 'b'로만 이루어지고, 각각 amber(호박색)과 brass(황동색)을 의미한다.
#   Malvika는 모든 풍선을 한 가지 색으로 통일시키고 싶어 한다.
#   이를 위해 일부 풍선을 다른 색으로 칠할 때, 최소 몇 개의 풍선을 칠해야 모든 풍선이 같은 색이 되는지 구하는 문제이다.
#
# 입력:
#   첫 번째 줄에 테스트 케이스의 개수 T가 주어진다.
#   이후, 각 테스트 케이스마다 한 줄에 풍선의 색을 나타내는 문자열 s가 주어진다.
#
# 출력:
#   각 테스트 케이스에 대해, 모든 풍선을 같은 색으로 만들기 위해 필요한 최소 풍선 칠하기 횟수를 출력한다.
#
# 해결 방법:
#   문자열 s에서 'a'와 'b'의 개수를 세고, 더 적은 개수를 가진 색을 반대로 칠하면 된다.
#
# 시간/공간 복잡도: O(n) (n: 문자열 길이)

T = int(input())
for _ in range(T):
    s = input().strip()
    print(min(s.count("a"), s.count("b")))