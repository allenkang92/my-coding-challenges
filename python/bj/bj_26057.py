# 문제 링크 (주석) : https://www.acmicpc.net/problem/26057
# 간단한 문제 설명 : 두 명의 참가자가 우유를 짜는 동안 총 우유 양 L과 우유가 흘러나온 시간 T가 주어졌을 때, 두 참가자의 우유 양 차이를 계산
# 해결 방법 설명 : 두 참가자의 우유 양 차이는 2 * T - L로 계산
# 시간/공간 복잡도 : O(1) (입력 크기에 관계없이 일정한 시간과 공간이 소요됨)

# 입력 받기
L = int(input())  # 총 우유 양
T = int(input())  # 우유가 흘러나온 시간

# 차이 계산
difference = (2 * T) - L

# 결과 출력
print(difference)