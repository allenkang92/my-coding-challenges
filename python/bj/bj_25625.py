# 문제 링크 : https://www.acmicpc.net/problem/25625
# 문제 설명:
#   대면 SNUPC에 맞춰 제2공학관으로 가기 위해 민준이는 셔틀버스를 타고자 한다.
#   셔틀버스는 서울대입구역과 제2공학관 사이를 운행하는데, 한 방향으로 이동하는 데 걸리는 시간은 x분이다.
#   현재 민준이는 서울대입구역에 있으며, 버스는 y분 후에 제2공학관에 도착할 예정이다.
#   민준이는 버스에 탑승하기 위해 서울대입구역에서 기다려야 하는 최소 시간을 구해야 한다.
#
#   버스는 쉬지 않고 운행하므로, 두 가지 경우가 존재한다.
#   1. y ≤ x인 경우: 현재 버스는 서울대입구역을 떠나 제2공학관으로 이동 중이다.
#      이 경우 버스는 y분 후에 제2공학관에 도착한 뒤, 즉시 서울대입구역으로 돌아오기 시작하므로,
#      서울대입구역에 도착하는 데 걸리는 시간은 y + x분이다.
#
#   2. y > x인 경우: 현재 버스는 제2공학관에서 서울대입구역으로 돌아오는 중이다.
#      이 경우 서울대입구역 도착까지 남은 시간은 (2×x - y)분이지만, 
#      문제에서 주어진 조건과 예제 (x=10, y=12 → 출력 2)를 고려하면,
#      서울대입구역에 도착하기까지 실제로 남은 시간은 y - x분이다.
#
#   따라서, 문제의 조건에 따라 (x, y)가 주어지면,
#   if y ≤ x: 기다려야 하는 최소 시간 = x + y,
#   else: 기다려야 하는 최소 시간 = y - x.
#
#   (단, 문제의 제약에서 x ≠ y이며, 1 ≤ y < 2x이므로, y == x인 경우는 발생하지 않습니다.)
#
# 시간/공간 복잡도 : O(1)

x, y = map(int, input().split(" "))

if x > y:
    print(x + y)
else:
    print(y - x)