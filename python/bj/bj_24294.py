# 문제 링크 (주석) : https://www.acmicpc.net/problem/24294
# 간단한 문제 설명 : 두 개의 직사각형이 나란히 놓여 있는 꽃밭의 둘레를 계산하는 문제
# 해결 방법 설명 : 두 직사각형의 외곽선 길이를 합하고, 겹치는 부분의 내부 경계선 길이를 빼서 둘레를 계산
# 시간/공간 복잡도 : O(1) (입력 크기에 관계없이 일정한 시간과 공간이 소요됨)

# 입력
w1 = int(input())  # 첫 번째 직사각형의 너비
h1 = int(input())  # 첫 번째 직사각형의 높이
w2 = int(input())  # 두 번째 직사각형의 너비
h2 = int(input())  # 두 번째 직사각형의 높이

# 출력
print(4 + 2 * max(w1, w2) + 2 * (h1 + h2))  # 둘레 계산 및 출력