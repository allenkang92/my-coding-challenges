# 문제 링크 : https://www.acmicpc.net/problem/15080
# 문제 설명:
#   택시 요금을 초 단위로 계산하기 위해,
#   승객이 택시에 탑승한 시각과 하차한 시각이 주어진다.
#   시각은 "hh : mm : ss" 형태로 주어지며, 24시간제를 사용한다.
#   단, 하차 시각이 탑승 시각보다 작을 경우는 자정이 넘어간 것으로 간주한다.
#   두 시각 사이의 경과 초를 구하여 출력하는 문제이다.
#
# 해결 방법 설명:
#   각 시각을 초 단위(시*3600 + 분*60 + 초)로 변환한 후,
#   만약 하차 시각의 총 초가 탑승 시각의 총 초보다 작으면 24시간(86400초)을 더해준다.
#   최종적으로 하차 시각 초에서 탑승 시각 초를 뺀 값을 출력한다.
#
# 시간/공간 복잡도 : O(1)

h1, m1, s1 = map(int, input().split(" : "))
h2, m2, s2 = map(int, input().split(" : "))

start = h1 * 3600 + m1 * 60 + s1
end = h2 * 3600 + m2 * 60 + s2

if end < start:
    end += 24 * 3600

print(end - start)