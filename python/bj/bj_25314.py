# 문제 링크 (주석) : https://www.acmicpc.net/problem/25314
# 간단한 문제 설명 : 
#   N바이트 크기의 정수를 저장할 수 있는 자료형을 출력
#   4바이트마다 'long'이 추가되며, 마지막에 'int'가 붙음
#
# 해결 방법 설명 : 
#   - N을 입력받아 4로 나눈 몫만큼 'long'을 반복
#   - 각 'long' 사이와 'int' 앞에 공백 추가
#   - 마지막에 'int' 붙여서 출력
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(N) - N/4번 문자열 연산
#   - 공간 복잡도: O(N) - 결과 문자열 저장

N = int(input())

print("long " * int(N//4) + "int")