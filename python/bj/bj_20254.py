# 문제 링크 : https://www.acmicpc.net/problem/20254
# 간단한 문제 설명 :
#   2020 타이페이-신주 지역 대회의 사이트 점수를 계산하는 문제.
#   사이트 점수는 아래 공식으로 계산된다:
#     Site Score = 56*UR + 24*TR + 14*UO + 6*TO
#   여기서,
#     UR: 지역 대회에서 최소 한 문제 이상 해결한 대학교 수
#     TR: 지역 대회에서 최소 한 문제 이상 해결한 팀 수
#     UO: TOPC에서 최소 한 문제 이상 해결한 대학교 수
#     TO: TOPC에서 최소 한 문제 이상 해결한 팀 수
# 해결 방법 설명 :
#   입력받은 네 정수에 각 가중치를 곱한 후, 모두 더하여 결과를 출력한다.
# 시간/공간 복잡도 : O(1)

UR, TR, UO, TO = map(int, input().split())

result = 56 * UR + 24 * TR + 14 * UO + 6 * TO
print(result)