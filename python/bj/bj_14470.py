# 문제 링크 (주석) : https://www.acmicpc.net/problem/14470
# 문제 설명:
#   JOI 군은 A℃의 고기를 전자레인지로 B℃까지 데우려고 한다.
#   고기가 0℃ 미만이면 얼어 있는 상태로 간주하며, 얼어 있는 고기를 1℃ 데우는 데에는 C초가 걸립니다.
#   얼어 있는 상태에서 온도가 정확히 0℃가 되면, 해동하는 데 D초가 추가로 걸립니다.
#   고기가 얼어 있지 않은 상태이면 1℃ 데우는 데에는 E초가 걸립니다.
#   단, 입력의 첫 번째 줄에 주어지는 고기의 온도 A는 0이 아니라 -100 이상 100 이하의 정수이며,
#   목표 온도 B는 1 이상 100 이하이며 A보다 큽니다.
#
# 해결 방법 설명:
#   - 만약 A가 0보다 작으면(고기가 얼어 있으면):
#       1) A℃에서 0℃까지 데우는 데 걸리는 시간은 |A| * C 초입니다.
#       2) 0℃인 얼어 있는 고기를 해동하는 데 D 초가 걸립니다.
#       3) 해동 후 0℃에서 B℃까지 데우는 데 B * E 초가 걸립니다.
#       따라서 총 시간은 (|A| * C) + D + (B * E) 입니다.
#   - 만약 A가 0보다 크면(고기가 얼어 있지 않으면):
#       A℃에서 B℃까지 데우는 데 걸리는 시간은 (B - A) * E 초입니다.
#
# 시간/공간 복잡도 : O(1)

A = int(input())
B = int(input())
C = int(input())
D = int(input())
E = int(input())

if A < 0:
    # 고기가 얼어 있는 경우
    print((abs(A) * C) + D + (B * E))
else:
    # 고기가 얼어 있지 않은 경우
    print((B - A) * E)