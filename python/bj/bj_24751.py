# 문제 링크 (주석) : https://www.acmicpc.net/problem/24751
# 간단한 문제 설명 : 두 가지 옵션에 베팅된 스위치 포인트의 비율을 기반으로, 각 옵션의 스위치-페이아웃-비율을 계산
# 해결 방법 설명 : 옵션 1의 페이아웃 비율은 100 / a, 옵션 2의 페이아웃 비율은 100 / (100 - a)
# 시간/공간 복잡도 : O(1) (입력 크기에 관계없이 일정한 시간과 공간이 소요됨)

# 입력 받기
a = int(input())

# 페이아웃 비율 계산
x1 = 100 / a
x2 = 100 / (100 - a)

# 결과 출력 (소수점 10자리까지)
print(f"{x1:.10f}")
print(f"{x2:.10f}")