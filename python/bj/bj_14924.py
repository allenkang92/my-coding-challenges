# 문제 링크 (주석) : https://www.acmicpc.net/problem/14924
# 간단한 문제 설명 : 두 기차가 서로를 향해 출발할 때, 파리가 두 기차 사이를 왔다 갔다 하며 이동한 총 거리를 계산하세요.
# 해결 방법 설명 : 두 기차가 만나는 시간을 계산하고, 그 시간 동안 파리가 이동한 거리를 구합니다.
# 시간/공간 복잡도 : O(1)

# 입력 받기
S, T, D = map(int, input().split())

# 두 기차가 만나는 시간 계산
time = D / (2 * S)

# 파리의 이동 거리 계산
F = T * time

# 결과 출력 (정수로 변환)
print(int(F))