# 문제 링크 : https://www.acmicpc.net/problem/13623
# 간단한 문제 설명 : 세 플레이어의 선택을 입력받아 승자를 판단하세요. 모두 같은 값을 선택하면 승자가 없고, 한 플레이어만 다른 값을 선택하면 그 플레이어가 승자입니다.
# 해결 방법 설명 : 세 플레이어의 선택을 비교하여 승자를 판단합니다.
# 시간/공간 복잡도 : O(1)

# 입력 받기
A, B, C = map(int, input().split())

# 승자 판단
if A == B == C:
    print("*")  # 승자 없음
elif A == B:
    print("C")  # 클라라가 승자
elif A == C:
    print("B")  # 밥이 승자
else:
    print("A")  # 앨리스가 승자