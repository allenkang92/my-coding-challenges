# 문제 링크 : https://www.acmicpc.net/problem/28113
# 간단한 문제 설명 :
#   찬솔이는 버스 정류장에 서 있으며, 마지막 버스는 A분 후 도착한다.
#   지하철역까지 걸어가는데 N분 걸리며, 마지막 지하철은 B분 후 도착한다.
#   (역에 도착하는 시간과 지하철 도착 시간이 같으면 탈 수 있음)
#   실제로 지하철 탑승 시간은 걸어가는 시간 N 후 기다려야 하는 시간(B - N)을 포함하여,
#   결과적으로 B분 후 탑승하게 됨.
#
# 해결 방법 설명 :
#   버스 탑승 시간은 A분, 지하철 탑승 시간은 B분이므로,
#   A와 B를 비교하여 더 빨리 탈 수 있는 대중교통 수단을 결정한다.
#   - A < B 이면 버스가 더 빠름 → "Bus"
#   - A > B 이면 지하철이 더 빠름 → "Subway"
#   - A == B 이면 둘 다 동일하므로 → "Anything"
#
# 시간/공간 복잡도 : O(1)

N, A, B = map(int, input().split())

if A < B:
    print("Bus")
elif A > B:
    print("Subway")
else:
    print("Anything")