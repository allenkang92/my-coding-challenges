# 문제 링크 : https://www.acmicpc.net/problem/27182
# 간단한 문제 설명: 
#   페트야는 매주 일요일마다 날씨를 기록한다.
#   현재 일요일의 날짜가 n이고, 2주 전 일요일의 날짜가 m일 때,
#   지난 주 일요일(1주 전)의 날짜를 구하는 문제이다.
#
# 해결 방법 설명:
#   1. 현재 일요일(n)이 8 이상이면, 현재 일요일에서 7일을 빼면 지난 주 일요일이 된다.
#   2. 현재 일요일(n)이 8 미만이면, 월이 바뀐 상황이므로 2주 전 일요일(m)에서 7일을 더하면 지난 주 일요일이 된다.
#   3. 조건 표현식을 사용하여 간결하게 구현할 수 있다.
#            
# 시간/공간 복잡도: O(1) - 단순 계산만 수행함

n, m = map(int, input().split())
print([m + 7, n - 7][n >= 8])