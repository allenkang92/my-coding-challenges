# 문제 링크 : https://www.acmicpc.net/problem/20353
# 문제 설명:
#   전통적인 로마 돔의 아트리움은 정사각형 모양의 공간입니다.
#   아트리움의 면적 a가 주어지면, 한 변의 길이는 √a이며 전체 둘레는 4 × √a가 됩니다.
#
# 해결 방법:
#   1. 입력받은 면적 a의 제곱근을 구합니다.
#   2. 그 값에 4를 곱하여 총 벽의 길이를 계산합니다.
#
# 시간/공간 복잡도 : O(1)

a = int(input())
total_length = (a ** 0.5) * 4
print(total_length)