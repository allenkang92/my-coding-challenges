# 문제 링크 : https://www.acmicpc.net/problem/19698
# 문제 설명:
#   농부 존은 소들을 위해 새로운 헛간을 지었다.
#   이번 추첨에는 총 N마리의 소가 응모했으며, 헛간은 W × H 크기의 직사각형이다.
#   베시는 소 한 마리당 L × L 크기의 공간을 배정할 계획이다.
#   헛간에 들어갈 수 있는 소의 수는 (W // L) * (H // L) 마리이다.
#   단, 응모한 소의 수 N마리보다 많을 수 없으므로, 최종 답은 min(N, (W // L) * (H // L)) 가 된다.
#
# 해결 방법:
#   W와 H에서 각각 L로 나눈 몫을 구하고 곱한 후, N과 비교하여 더 작은 값을 출력한다.
#
# 시간/공간 복잡도 : O(1)

N, W, H, L = map(int, input().split(" "))

max_cows = (W // L) * (H // L)
print(min(N, max_cows))