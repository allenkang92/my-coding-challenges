# 문제 링크 : https://www.acmicpc.net/problem/32929
# 문제 설명:
#   UOS 문자열은 "UOS"가 무한히 반복되는 문자열입니다.
#   양의 정수 x가 주어질 때, UOS 문자열의 x번째 문자를 구하는 문제입니다.
#
# 해결 방법 설명:
#   UOS 문자열은 "U", "O", "S"가 순서대로 반복되므로,
#   x를 3으로 나눈 나머지에 따라 x번째 문자를 결정합니다.
#   - x % 3 == 1이면 "U"
#   - x % 3 == 2이면 "O"
#   - x % 3 == 0이면 "S"
#
# 시간/공간 복잡도 : O(1)

x = int(input())

if x % 3 == 0: 
    print("S")
elif x % 3 == 2:
    print("O")
elif x % 3 == 1:
    print("U")