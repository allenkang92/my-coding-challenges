# 문제 링크 : https://www.acmicpc.net/problem/31611
# 간단한 문제 설명 :
#   오늘은 일요일이다. 정수 X가 주어졌을 때, 오늘로부터 X일 후가 화요일이면 1, 아니면 0을 출력한다.
#
# 해결 방법 설명 :
#   일요일을 0, 월요일을 1, 화요일을 2, …, 토요일을 6으로 두면,
#   오늘이 일요일인 상황은 시작 인덱스가 0이므로, X일 후의 요일은 (X % 7)이다.
#   따라서, (X % 7) == 2면 화요일이므로 1, 그렇지 않으면 0을 출력한다.
#
# 시간/공간 복잡도 : O(1)

X = int(input())

if X % 7 == 2:
    print(1)
else:
    print(0)