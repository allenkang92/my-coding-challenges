# 문제 링크 (주석) : https://www.acmicpc.net/problem/21612
# 문제 설명:
#   해수면에서는 대기 압력이 100 kPa이고 물은 100℃에서 끓습니다.
#   해수면보다 높아지면 대기 압력이 낮아져 물의 끓는점이 낮아지고,
#   해수면보다 낮아지면 대기 압력이 높아져 물의 끓는점이 높아집니다.
#   물의 끓는점 B(℃)와 대기 압력 P(kPa) 사이에는 다음과 같은 관계가 있습니다.
#       P = 5 × B − 400
#
#   B가 주어지면 대기 압력을 구하고, 해수면보다 낮은지(대기압 100 kPa보다 큼), 해수면인지(대기압 100 kPa), 
#   또는 해수면보다 높은지(대기압 100 kPa보다 작음)를 판단하여 각각 -1, 0, 1을 출력합니다.
#
# 해결 방법:
#   B를 입력받아 P = 5×B − 400를 계산한 뒤,
#   P가 100 kPa보다 크면 해수면보다 낮으므로 -1,
#   P가 100 kPa와 같으면 해수면이므로 0,
#   그렇지 않으면 해수면보다 높으므로 1을 출력합니다.
#
# 시간/공간 복잡도 : O(1)

B = int(input())
P = (B * 5) - 400

print(P)
if P > 100:
    print(-1)
elif P == 100:
    print(0)
else:
    print(1)