# 문제 링크 (주석) : https://www.acmicpc.net/problem/13752
# 문제 설명:
#   주어진 테스트 케이스마다, 정수 k에 대해 k개의 '='를 공백 없이 출력하는 히스토그램을 생성하는 문제입니다.
#
# 해결 방법 설명:
#   첫 번째 줄에 테스트 케이스의 개수 n이 주어지고,
#   이후 n개의 줄에 각 히스토그램의 크기를 나타내는 정수 k가 주어집니다.
#   각 테스트 케이스마다, '='를 k번 반복하여 출력합니다.
#
# 시간/공간 복잡도 : O(n)

n = int(input())

for _ in range(n):
    k = int(input())
    print("=" * k)