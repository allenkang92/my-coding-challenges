# 문제 링크 (주석) : https://www.acmicpc.net/problem/27239
# 간단한 문제 설명 : 체스판의 각 칸에 번호를 매긴 후, 주어진 번호에 해당하는 칸의 위치를 출력
# 해결 방법 설명 : n에 해당하는 행과 열을 계산하고, 열을 알파벳 문자로 변환하여 출력
# 시간/공간 복잡도 : O(1) (상수 시간과 공간이 소요됨)

# 입력 받기
n = int(input())

# 행과 열 계산
row = (n - 1) // 8 + 1
col = (n - 1) % 8

# 열을 알파벳 문자로 변환 (0 -> 'a', 1 -> 'b', ..., 7 -> 'h')
col_char = chr(ord('a') + col)

# 결과 출력
print(f"{col_char}{row}")