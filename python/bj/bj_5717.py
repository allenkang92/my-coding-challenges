# 문제 링크 (주석) : https://www.acmicpc.net/problem/5717
# 문제 설명:
#   상근이의 남자 친구의 수(M)와 여자 친구의 수(F)가 주어질 때, 친구의 총 수(M+F)를 구하는 문제입니다.
#   입력의 마지막 줄에는 0 0이 주어지며, 이때 프로그램은 종료됩니다.
#
# 해결 방법 설명:
#   모든 테스트 케이스에 대해, 두 정수를 입력받아 합을 계산하고 출력합니다.
#
# 시간/공간 복잡도 : O(1)

while True:
    M, F = map(int, input().split())
    if M == 0 and F == 0:
        break
    print(M + F)