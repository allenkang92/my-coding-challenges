# 문제 링크 : https://www.acmicpc.net/problem/15873
# 문제 설명:
#   공백 없이 붙어 있는 자연수 A, B가 입력으로 주어질 때, A+B의 값을 출력하는 문제입니다.
#   (단, 0 < A, B ≤ 10)
#
# 해결 방법 설명:
#   입력 문자열의 길이에 따라, A와 B가 몇 자리 수인지를 판단합니다.
#   - 길이가 2인 경우: 두 수 모두 한 자리.
#   - 길이가 3인 경우: 둘 중 하나가 10.
#       만약 문자열이 "10X"라면 A=10, B=X.
#       그렇지 않으면 문자열이 "X10"이므로 A=X, B=10.
#   - 길이가 4인 경우: 두 수 모두 10.
#
# 시간/공간 복잡도 : O(1)

AB = input()
L = len(AB)

if L == 2:
    A = int(AB[0])
    B = int(AB[1])
elif L == 3:
    if AB.startswith("10"):
        A = 10
        B = int(AB[2])
    else:
        A = int(AB[0])
        B = 10
elif L == 4:
    A = 10
    B = 10

print(A + B)