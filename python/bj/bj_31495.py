# 문제 링크 : https://www.acmicpc.net/problem/31495
# 문제 설명:
#   토카가 작성한 문자열 출력 코드에서, 큰따옴표(")가 올바르게 사용되었는지 판별하는 문제이다.
#   "정확한 문자열"은 문자열의 시작과 끝이 큰따옴표로 감싸져 있으며, 그 내부에 하나 이상의 문자가 존재해야 한다.
#   만약 토카의 문자열이 정확한 문자열이면 큰따옴표 안쪽의 내용을 출력하고,
#   그렇지 않으면 "CE"를 출력한다.
#
# 입력:
#   첫 번째 줄에 토카의 문자열 S가 주어진다.
#   S는 최대 50글자이며, 영어 대소문자, 공백, 그리고 큰따옴표(")로만 이루어져 있다.
#   큰따옴표는 전체 문자열에서 최대 2개 존재함이 보장된다.
#
# 출력:
#   토카의 문자열이 정확한 문자열이면 큰따옴표 내부의 문자열을 출력하고,
#   그렇지 않거나 내부가 빈 문자열이면 "CE"를 출력한다.
#
# 해결 방법:
#   문자열 S가 최소 2글자 이상이고, 첫 글자와 마지막 글자가 모두 큰따옴표(")인지 확인한다.
#   또한, 두 큰따옴표 사이에 문자가 한 개 이상 있는지 확인한 후 조건에 따라 출력을 결정한다.
#
# 시간/공간 복잡도: O(1)

S = input()

if len(S) >= 2 and S[0] == '"' and S[-1] == '"' and len(S[1:-1]) > 0:
    print(S[1:-1])
else:
    print("CE")