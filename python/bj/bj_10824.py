# 문제 링크 : https://www.acmicpc.net/problem/10824
# 간단한 문제 설명 : 네 자연수 A, B, C, D가 주어질 때, A와 B를 붙인 수와 C와 D를 붙인 수의 합을 구하는 문제
# 해결 방법 설명 : 문자열 연결을 통해 숫자를 붙인 후, 정수로 변환하여 합을 계산            
# 시간/공간 복잡도 : O(1) - 입력이 고정된 네 개의 숫자로 제한됨

# 네 개의 자연수 입력 받기
A, B, C, D = input().split(" ")

# A와 B를 붙인 수와 C와 D를 붙인 수 계산 후 합산
print(int(A + B) + int(C + D))