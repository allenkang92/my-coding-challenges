# 문제 링크 : https://www.acmicpc.net/problem/24309
# 문제 설명:
#   주어진 자연수 a, b, c에 대해 등식 a * x = b - c를 만족하는 x를 구하는 문제입니다.
#   (조건: 0 < c < b ≤ 10^100, 0 < a ≤ 10^17)
#
# 해결 방법:
#   등식 a * x = b - c를 x에 대해 정리하면, x = (b - c) // a 입니다.
#   파이썬은 큰 정수를 처리할 수 있으므로, 바로 계산하여 출력할 수 있습니다.
#
# 입력:
#   각 줄마다 a, b, c가 차례로 주어집니다.
#
# 출력:
#   등식을 만족하는 자연수 x를 출력합니다.

a = int(input())  # x의 계수 a 입력
b = int(input())  # 수 b 입력
c = int(input())  # 수 c 입력

# 등식 a * x = b - c를 x에 대해 풀면 x = (b - c) // a 가 됩니다.
x = (b - c) // a

print(x)  # x 출력