# 문제 링크 (주석) : https://www.acmicpc.net/problem/1330
# 간단한 문제 설명 : 
# 두 정수 A와 B가 주어질 때, A와 B를 비교하여 A가 B보다 크면 '>'를, 작으면 '<'를, 같으면 '=='를 출력하는 프로그램을 작성한다.
#
# 해결 방법 설명 : 
# 입력으로 두 정수를 받아서 비교 연산을 수행한 후, 조건에 맞는 기호를 출력한다.
# 파이썬의 if-elif-else 문을 사용하여 각 조건을 처리한다.
#
# 시간/공간 복잡도 : 
# 시간 복잡도: O(1) - 상수 시간에 실행됨
# 공간 복잡도: O(1) - 상수 공간을 사용함

A, B = map(int, input().split())

if A == B:
    print("==")
elif A > B:
    print(">")
else:
    print("<")