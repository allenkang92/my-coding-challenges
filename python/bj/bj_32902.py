# 문제 링크 : https://www.acmicpc.net/problem/32902
# 간단한 문제 설명:
#   Vasiliy는 k개의 감자칩 캔(각각 n개의 칩 보유)을 가지고 매 분마다 한 칩씩 먹음.
#   어느 시점에 캔을 들었을 때 그 캔이 비어 있었다면,
#   그 시점(몇 분 후인가)의 최소값과 최대값을 구하는 문제.
# 
# 해결 방법:
#   1) 최소 시간: 한 캔에서만 집중적으로 칩을 먹으면 n분 후 그 캔이 비게 됨.
#      바로 다음 분(n+1분째)에 그 캔을 또 집으면 빈 캔을 발견.
#      → min_time = n + 1
#   2) 최대 시간: 모든 캔을 최대한 균등하게 먹으면 k*n개의 칩이 동시에 소진.
#      정확히 k*n분 동안 계속 칩을 먹어서 모두 소진된 뒤, (k*n+1)분째에 어떤 캔을 집어도 비어 있음.
#      → max_time = k*n + 1
#
# 시간 복잡도: O(1)

k, n = map(int, input().split())

min_time = n + 1
max_time = k * n + 1

print(min_time, max_time)