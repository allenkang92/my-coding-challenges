# 문제 링크 : https://www.acmicpc.net/problem/8393
# 간단한 문제 설명 : 
#   n이 주어졌을 때, 1부터 n까지의 합을 구하는 프로그램을 작성한다.
#   n은 1보다 크거나 같고, 10,000보다 작거나 같다.
#
# 해결 방법 설명 : 
#   - n을 입력받아 정수로 변환한다.
#   - range 함수를 사용하여 1부터 n까지 반복하며 합을 계산한다.
#   - 누적된 합을 출력한다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(n) - n번의 반복
#   - 공간 복잡도: O(1) - 상수 공간만 사용

n = int(input())
count_n = 0
for i in range(1, n+1):  # 0부터가 아닌 1부터 시작
    count_n += i
    
print(count_n)