# 문제 링크 : https://www.acmicpc.net/problem/21955
# 간단한 문제 설명 : 짝수 개의 숫자를 가진 수를 입력받아 정확히 절반으로 나누어 두 개의 숫자로 출력하는 문제
# 해결 방법 설명 : 문자열로 입력받은 숫자를 절반으로 나누어 두 개의 문자열로 분리한 후 출력
# 시간/공간 복잡도 : O(1), 입력 숫자의 길이에 관계없이 상수 시간에 처리

# 숫자를 문자열로 입력받음
N = input()

# 입력된 문자열의 길이 계산
N_len = len(N)

# 문자열의 앞 절반을 첫 번째 숫자로 설정
first = N[:N_len//2]

# 문자열의 뒷 절반을 두 번째 숫자로 설정
last = N[N_len//2:]

# 나눠진 두 숫자를 공백으로 구분하여 출력
print(first, last)