# 문제 링크 (주석) : https://www.acmicpc.net/problem/2444
# 간단한 문제 설명 : N을 입력받아 다이아몬드 모양의 별을 출력합니다.
# 해결 방법 설명 : 1. 상단부: 공백은 감소, 별은 2개씩 증가
#                2. 하단부: 공백은 증가, 별은 2개씩 감소
# 시간/공간 복잡도 : O(n) (n은 입력받은 수)

N = int(input())

# 상단부 (1줄부터 N줄까지)
for i in range(1, N+1):
    print(" " * (N - i) + "*" * (2*i - 1))

# 하단부 (N-1줄부터 1줄까지)
for i in range(N-1, 0, -1):
    print(" " * (N - i) + "*" * (2*i - 1))