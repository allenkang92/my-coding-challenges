# 문제 링크 (주석) : https://www.acmicpc.net/problem/31261
# 문제 설명:
#   브라보! 브랜디는 형제에게 “내가 한 숫자를 생각했어.
#   그 숫자를 a로 나눈 뒤 a를 빼고, 그 결과에서 다시 같은 연산을 수행하면 b가 나온다”라고 말했다.
#   주어진 a와 b에 대해, 브라보가 생각한 숫자를 구하는 프로그램을 작성하라.
#
# 해설:
#   문제의 설명을 수식으로 표현하면 다음과 같다.
#   임의의 숫자 x에 대해, 첫 번째 연산은:
#       T(x) = (x / a) - a
#   두 번째 연산은:
#       T(T(x)) = ((x / a - a) / a) - a = x / (a^2) - (a + 1)
#   이때 T(T(x))가 b가 되어야 하므로,
#       x / (a^2) - (a + 1) = b
#   양변에 (a^2)를 곱하면:
#       x = a^2 * (b + a + 1)
#
# 입력:
#   두 정수 a와 b가 공백으로 구분되어 주어진다.
#
# 출력:
#   브라보가 생각한 숫자를 출력한다.
#
# 제한:
#   1 ≤ a ≤ 100, 1 ≤ b ≤ 100
#
# 시간/공간 복잡도: O(1)

a, b = map(int, input().split())
result = (a ** 2) * (b + a + 1)
print(result)