# 문제 링크 (주석) : https://www.acmicpc.net/problem/2438
# 간단한 문제 설명 : 
#   N번째 줄에 별 N개를 출력하는 문제
#
# 해결 방법 설명 : 
#   - N을 입력받음
#   - 1부터 N까지 반복하면서 각 줄에 i개의 별을 출력
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(N^2) - 각 줄마다 i개의 별을 출력
#   - 공간 복잡도: O(1) - 상수 공간만 사용

N = int(input())

for i in range(1, N+1):  # 1부터 N까지 반복
    print("*" * i)