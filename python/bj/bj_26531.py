# 문제 링크 (주석) : https://www.acmicpc.net/problem/26531
# 문제 설명:
#   농장에서 재고 관리를 돕기 위해 고용한 소 젖소 관리인이 두 숫자의 합산을 제대로 수행하는지 확인해야 합니다.
#   입력은 다음과 같은 형식의 문자열입니다:
#       a + b = c
#   a, b, c는 모두 한 자리 정수이며, 입력은 공백을 포함하여 정확히 9글자입니다.
#
# 출력:
#   주어진 수식이 올바른 합계를 나타내면 "YES"를, 그렇지 않으면 "NO"를 출력합니다.
#
# 해결 방법:
#   입력 문자열을 '=' 기호를 기준으로 좌변과 우변으로 분리한 후,
#   좌변을 eval 함수를 사용하여 계산하고, 우변은 정수로 변환하여 비교합니다.
#
# 시간/공간 복잡도 : O(1)

formula = list(input().split("="))

if eval(formula[0]) == int(formula[-1]):
    print("YES")
else:
    print("NO")