# 문제 링크 : https://www.acmicpc.net/problem/15439
# 문제 설명:
#   베라는 상의 N벌과 하의 N벌이 있다.
#   i번째 상의와 i번째 하의는 모두 색상 i를 가지고 있으며, 모든 색상은 서로 다르다.
#   상의와 하의가 서로 다른 색상인 조합의 가짓수를 구하는 문제입니다.
#
# 해결 방법 설명:
#   상의와 하의의 모든 조합은 N * N 가지이며, 같은 색상의 조합은 N가지입니다.
#   따라서 서로 다른 색상의 조합의 총 개수는 N * N - N = N × (N - 1) 입니다.
#
# 시간/공간 복잡도 : O(1)

N = int(input())
print(N * (N - 1))