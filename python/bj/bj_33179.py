# 문제 링크 : https://www.acmicpc.net/problem/33179
# 간단한 문제 설명 : 
#   헤자르다스탄 회사의 연간 보고서를 양면 인쇄할 때 필요한 A4 용지 수 계산하기
#   - 각 챕터는 항상 홀수 페이지에서 시작해야 함
#   - 서로 다른 챕터의 내용이 한 종이에 함께 인쇄되면 안 됨
#   - 홀수 페이지 수를 가진 챕터 뒤에는 빈 페이지 추가 필요
#
# 해결 방법 설명 :            
#   1. 각 챕터의 페이지 수를 더함
#   2. 홀수 페이지 챕터 뒤에는 빈 페이지 하나를 추가 (다음 챕터가 홀수 페이지에서 시작하도록)
#   3. 총 페이지 수를 2로 나누어 필요한 A4 용지 수 계산 (한 장에 양면으로 2페이지 인쇄)
#
# 시간/공간 복잡도 : O(n) - n은 챕터의 수

# 입력 처리
n = int(input())
pages = list(map(int, input().split()))

# 총 페이지 수 계산
total_pages = 0
for page in pages:
    # 각 챕터의 페이지 수 추가
    total_pages += page
    
    # 홀수 페이지를 가진 챕터라면 빈 페이지 하나 추가
    if page % 2 == 1:
        total_pages += 1

# 총 페이지 수를 2로 나누어 필요한 A4 용지 수 계산
min_sheets = total_pages // 2

# 결과 출력
print(min_sheets)