# 문제 링크 : https://www.acmicpc.net/problem/11024
# 간단한 문제 설명 : 여러 테스트 케이스에서 각 줄에 주어진 N개 숫자들의 합을 구하는 문제
# 해결 방법 설명 : 각 테스트 케이스마다 공백으로 구분된 숫자들을 정수 리스트로 변환하여 합계 계산
# 시간/공간 복잡도 : O(T*N), T는 테스트 케이스 수, N은 각 케이스의 숫자 개수

# 테스트 케이스의 개수 T 입력
N = int(input())

# 각 테스트 케이스에 대해 처리
for _ in range(N):
    # 공백으로 구분된 정수들을 리스트로 변환
    N_list = list(map(int, input().split(" ")))
    # 리스트의 모든 요소 합을 출력
    print(sum(N_list))