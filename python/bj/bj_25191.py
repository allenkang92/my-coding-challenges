# 문제 링크 (주석) : https://www.acmicpc.net/problem/25191
# 간단한 문제 설명 : 치킨집에 있는 치킨의 개수와 임스의 집에 있는 콜라, 맥주의 개수를 고려하여 임스가 먹을 수 있는 치킨의 총 개수를 계산
# 해결 방법 설명 : 콜라와 맥주의 수를 고려하여 임스가 먹을 수 있는 치킨의 최대 개수를 계산하고, 치킨집의 치킨 개수와 비교하여 최종 값을 결정
# 시간/공간 복잡도 : O(1) (입력 크기에 관계없이 일정한 시간과 공간이 소요됨)

# 입력 받기
N = int(input())  # 치킨집에 있는 치킨의 총 개수
A, B = map(int, input().split())  # 임스의 집에 있는 콜라와 맥주의 개수

# 임스가 먹을 수 있는 치킨의 총 개수 계산
max_chicken = min(N, (A // 2) + B)

# 결과 출력
print(max_chicken)