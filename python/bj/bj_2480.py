# 문제 링크 (주석) : https://www.acmicpc.net/problem/2480
# 간단한 문제 설명 : 
#   1에서부터 6까지의 눈을 가진 3개의 주사위를 던져서 다음과 같은 규칙에 따라 상금을 받는 게임이 있다.
#   같은 눈이 3개가 나오면 10,000원+(같은 눈)×1,000원의 상금을 받게 된다.
#   같은 눈이 2개만 나오는 경우에는 1,000원+(같은 눈)×100원의 상금을 받게 된다.
#   모두 다른 눈이 나오는 경우에는 (그 중 가장 큰 눈)×100원의 상금을 받게 된다.
#
# 해결 방법 설명 : 
#   - 세 개의 주사위 눈을 입력받아 리스트에 저장한다.
#   - 리스트를 정렬하여 중간값과 최댓값을 쉽게 찾을 수 있도록 한다.
#   - 세 눈이 모두 같은 경우, 두 눈만 같은 경우, 모두 다른 경우를 구분하여 상금을 계산한다.
#   - 두 눈이 같은 경우는 정렬된 리스트의 중간값이 반드시 같은 눈 중 하나가 된다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 상수 시간에 실행됨 (리스트 정렬도 원소가 3개로 고정)
#   - 공간 복잡도: O(1) - 상수 공간만 사용 (3개 원소의 리스트)

A, B, C = map(int, input().split())

dice = []
dice.append(A)
dice.append(B)
dice.append(C)
dice.sort()  # dice = dice.sort() 대신 이렇게만 사용

if A == B and B == C:
    print(10000 + (A * 1000))
elif (A == B and A != C) or (A == C and A != B) or (B == C and B != A):
    print(1000 + (dice[1] * 100))
else:
    print(dice[2] * 100)