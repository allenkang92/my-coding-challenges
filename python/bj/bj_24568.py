# 문제 링크 (주석) : https://www.acmicpc.net/problem/24568
# 간단한 문제 설명 :
#   정규 박스는 컵케이크 8개, 작은 박스는 컵케이크 3개를 담고 있다.
#   28명의 학생이 있으며, 모든 학생에게 컵케이크를 하나씩 나눠줄 때 남는 컵케이크의 개수를 구하는 문제입니다.
#
# 해결 방법 설명 :
#   총 컵케이크의 수를 구한 뒤, 28명의 학생에게 각각 1개씩 나누어주므로 남는 컵케이크 수는
#   (총 컵케이크 수) - 28로 계산할 수 있습니다.
#   (문제에서 최소 28개 이상의 컵케이크가 있다고 가정합니다.)
#
# 시간/공간 복잡도 : O(1)

# 입력:
#   첫 번째 줄 : 정규 박스의 개수 R (R ≥ 0)
#   두 번째 줄 : 작은 박스의 개수 S (S ≥ 0)
R = int(input())
S = int(input())

students = 28  # 학생의 수

# 총 컵케이크 수 계산:
# 정규 박스의 컵케이크: R * 8
# 작은 박스의 컵케이크: S * 3
total_cupcake = (R * 8) + (S * 3)

# 남는 컵케이크 수: 총 컵케이크 수 - 학생 수
print(total_cupcake - students)