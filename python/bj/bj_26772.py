# 문제 링크 (주석) : https://www.acmicpc.net/problem/26772
# 문제 설명:
#   Bajtek은 아름다운 Bajtolinie에게 사랑을 고백하기 위해 ASCII art로 N개의 하트(serduszka, hearts)를 가로로 나란히 출력하려고 합니다.
#   각 하트는 아래와 같이 구성되며, 하트와 하트 사이에는 정확히 1칸의 공백이 있어야 합니다.
#
#     @@@   @@@ 
#    @   @ @   @
#    @    @    @
#    @         @
#     @       @ 
#      @     @  
#       @   @   
#        @ @    
#         @     
#
#   입력으로 자연수 N이 주어지며 (1 ≤ N ≤ 100000),
#   출력은 위의 하트 모양을 N개 가로로 이어서 보여주어야 합니다.
#
# 해결 방법:
#   하트 모양의 각 줄을 문자열로 저장한 후, 각 줄에 대해 동일한 줄을 N개를 1칸의 공백으로 구분해 출력합니다.
#
# 시간/공간 복잡도 : O(N)
 
N = int(input())
heart = [
    " @@@   @@@ ",
    "@   @ @   @",
    "@    @    @",
    "@         @",
    " @       @ ",
    "  @     @  ",
    "   @   @   ",
    "    @ @    ",
    "     @     "
]

for line in heart:
    output_line = " ".join([line] * N)
    print(output_line)