# 문제 링크 : https://www.acmicpc.net/problem/2914
# 간단한 문제 설명 : 앨범 곡 수(A)와 저작권 있는 멜로디의 평균값(I)이 주어질 때, 최소 멜로디 개수를 구하는 문제
# 해결 방법 설명 : 평균값 I는 (저작권 있는 멜로디 수 / 곡 수)를 올림한 값이므로, 최소값은 A*(I-1)+1
# 시간/공간 복잡도 : O(1) - 단순 사칙연산만 수행

# 입력: A(앨범에 수록된 곡의 개수), I(올림한 평균값)
A, I = map(int, input().split())

# 저작권 있는 멜로디의 최소 개수 계산
# (I-1)은 올림하기 전 가능한 최대 평균값
# A*(I-1)은 평균이 정확히 I-1일 때의 멜로디 수
# +1을 하면 평균이 I-1보다 조금 커져서 올림하면 I가 됨
result = A * (I - 1) + 1

# 결과 출력
print(result)