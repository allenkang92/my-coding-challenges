# 문제 링크 (주석) : https://www.acmicpc.net/problem/2525
# 간단한 문제 설명 : 
#   주어진 현재 시각과 요리하는 데 필요한 시간을 분 단위로 입력받아, 
#   오븐구이가 끝나는 시각을 계산하는 프로그램을 작성한다.
#   시는 0부터 23까지, 분은 0부터 59까지의 정수이다.
#   시간이 24시를 넘으면 다음 날로 넘어간다.
#
# 해결 방법 설명 : 
#   - 현재 시각의 시(A)와 분(B)을 입력받는다.
#   - 요리하는 데 필요한 시간(C)을 입력받는다.
#   - C를 시와 분으로 나눈다.
#   - 분을 계산할 때 60을 넘으면 시를 추가로 더해준다.
#   - 시가 24를 넘으면 24로 나눈 나머지를 사용하여 하루를 넘지 않도록 한다.
#   - 계산된 시와 분을 출력한다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 상수 시간에 실행됨
#   - 공간 복잡도: O(1) - 상수 공간을 사용함

# 시, 분을 입력받고 정수로 변환
A, B = map(int, input().split(" "))
C = int(input())

# 요리하는 데 필요한 시간 시와 분으로 분할
H = C // 60
M = C % 60

# 분이 60 이상일 때 시를 추가하고 분을 재조정
A += H
B += M
if B >= 60:
    A += 1
    B -= 60

# 시가 24 이상일 경우 24로 나눈 나머지를 사용
if A >= 24:
    A = A % 24

# 종료 시각 출력
print(A, B)