# 문제 링크 : https://www.acmicpc.net/problem/21335
# 문제 설명:
#   아이슬란드 Geldingadalur에서 화산 분출로 가스가 원형 영역에 균일하게 퍼졌습니다.
#   당국은 이 영역을 둘러싸기 위해 방호 테이프를 설치하려고 합니다.
#   주어진 가스가 퍼진 총 면적 a가 주어질 때, 해당 원의 둘레(방호 테이프 길이)를 구하는 프로그램을 작성하시오.
#
# 해결 방법:
#   원의 면적 A는 π * r^2이며, 여기서 r = √(a / π) 입니다.
#   원의 둘레는 2 × π × r 이므로, 최종적으로 구해야 할 길이는 2 × π × √(a / π) 입니다.
#   이는 2 × √(a × π)와 동일합니다.
#   문제에서는 π의 값을 3.141592로 사용합니다.
#
# 시간/공간 복잡도 : O(1)

a = int(input())
pi = 3.141592
result = 2 * (a * pi) ** 0.5
print(result)