# 문제 링크 : https://www.acmicpc.net/problem/32215
# 간단한 문제 설명 : n대의 컴퓨터 각각에 m개의 에디터를 설치해야 하는데, 도훈이는 k개의 컴퓨터에 각각 m개의 에디터를 하나씩 설치하고, 차형준 선생님은 별도의 노트북에 m개의 에디터를 설치한 후 매크로를 사용하여 나머지 컴퓨터에 에디터를 일괄 설치합니다. 이때, 도훈이와 차형준 선생님이 직접 설치한 총 횟수를 계산
# 해결 방법 설명 : 도훈이의 설치 횟수는 k * m, 차형준 선생님의 설치 횟수는 m
# 시간/공간 복잡도 : O(1)

# 입력 처리
n, m, k = map(int, input().split())

# 총 설치 횟수 계산
total_installs = k * m + m

# 결과 출력
print(total_installs)