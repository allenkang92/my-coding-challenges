# 문제 링크 : https://www.acmicpc.net/problem/26545
# 간단한 문제 설명 : 
#   n개의 정수를 입력받아 모두 더한 값을 출력하는 문제입니다.
#
# 해결 방법 설명 :            
#   1. 먼저 더할 정수의 개수 n을 입력받습니다.
#   2. n번 반복하며 정수를 입력받아 리스트에 추가합니다.
#   3. sum() 함수를 사용하여 리스트의 모든 요소를 더한 값을 출력합니다.
#
# 시간/공간 복잡도 : O(n)

n = int(input())      # 더할 정수의 개수 입력

sum_list = []        # 입력받은 정수를 저장할 리스트
for _ in range(n):   # n번 반복
    sum_list.append(int(input()))  # 정수를 입력받아 리스트에 추가

print(sum(sum_list)) # 리스트의 모든 요소의 합 출력