# 문제 링크: https://www.acmicpc.net/problem/16600
# 문제 설명:
#   현대미술 전시에서, 정사각형 모양의 액자에 전기 필라멘트를 둘러 액자를 장식하려고 합니다.
#   이미지의 면적 a가 주어지면, 액자의 둘레(필라멘트의 길이)를 구하는 문제입니다.
#   액자는 정사각형이므로 한 변의 길이는 √a이며, 전체 둘레는 4 × √a 입니다.
#
# 해결 방법 설명:
#   1. 입력된 면적 a의 제곱근을 구하여 한 변의 길이를 계산합니다.
#   2. 그 후 한 변의 길이에 4를 곱하여 전체 둘레를 구합니다.
#
# 시간/공간 복잡도: O(1)

a = float(input())
side = a ** 0.5
print(side * 4)