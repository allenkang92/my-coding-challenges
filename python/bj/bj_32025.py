# 문제 링크 (주석) : https://www.acmicpc.net/problem/32025
# 문제 설명:
#   직사각형 운동장이 주어졌을 때, 그 안에 그릴 수 있는 가장 큰 원의 반지름을 구하는 문제입니다.
#   원은 직사각형 안에 완전히 포함되어야 하므로, 원의 지름은 운동장의 짧은 변의 길이와 같아야 합니다.
#
# 해결 방법 설명:
#   운동장의 한 변의 길이 H와 W가 주어지면, 원의 최대 지름은 min(H, W)이 됩니다.
#   따라서 원의 반지름은 min(H, W) / 2이고, 이를 센티미터 단위로 변환하기 위해 100을 곱합니다.
#
# 시간/공간 복잡도 : O(1)

H = int(input())
W = int(input())

# 운동장의 가장 짧은 변의 길이를 찾아서, 반지름을 구한 후 센티미터 단위로 변환
radius_cm = int(min(H, W) / 2 * 100)
print(radius_cm)