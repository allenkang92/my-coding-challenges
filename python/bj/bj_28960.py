# 문제 링크 : https://www.acmicpc.net/problem/28960
# 문제 설명:
#   닥터 스트레인지는 세탁 후에 플라щ(망토)를 널기 위해, 높이 h(cm)와 길이 l(cm)의 옷걸이(세탁용 로프)를 사용합니다.
#   스트레인지의 망토는 a와 b(cm)의 두 변을 가지는 직사각형 모양입니다.
#   망토를 로프 위에 걸 때, 망토를 반으로 접어서 두 쪽이 내려오게 됩니다.
#   즉, 망토의 한 변(즉, 접힌 후에 내려오는 부분)의 길이의 절반이 옷걸이에서 바닥까지의 높이 h보다 작거나 같아야 합니다.
#   또한, 망토를 걸 때 로프에 걸리는 가로 길이는 망토의 다른 변(접히지 않은 변)과 같으므로, 이 길이가 로프의 길이 l보다 작거나 같아야 합니다.
#
#   따라서 망토를 걸 수 있는 두 가지 경우를 볼 수 있습니다:
#     1. 망토의 a를 로프에 평행하게 하고, b의 절반(b/2)이 h 이하인 경우 (즉, a ≤ l and b ≤ 2*h)
#     2. 망토의 b를 로프에 평행하게 하고, a의 절반(a/2)이 h 이하인 경우 (즉, b ≤ l and a ≤ 2*h)
#
#   위 조건 중 하나라도 만족하면 "YES", 그렇지 않으면 "NO"를 출력합니다.
#
# 시간/공간 복잡도 : O(1)

h, l, a, b = map(int, input().split(" "))

if (a <= l and b <= 2 * h) or (b <= l and a <= 2 * h):
    print("YES")
else:
    print("NO")