# 문제 링크 : https://www.acmicpc.net/problem/25591
# 간단한 문제 설명 : 인도의 베다수학 곱셈법을 사용하여 두 수를 곱하는 과정을 구현
# 해결 방법 설명 : 두 수를 100에서 뺀 값을 사용하여 곱셈을 간단히 계산
# 시간/공간 복잡도 : O(1) (입력 크기에 관계없이 일정한 시간과 공간이 소요됨)

# 입력 받기
x, y = map(int, input().split())

# 베다수학 곱셈법 계산
a = 100 - x
b = 100 - y
c = 100 - (a + b)
d = a * b
q = d // 100
r = d % 100

# 결과 출력
print(a, b, c, d, q, r)
print(c + q, r)