# 문제 링크 (주석) : https://www.acmicpc.net/problem/26530
# 문제 설명:
#   동물의 부산물을 판매하여 추가 수입을 올리려고 합니다.
#   배송 원장을 보면, 여러 데이터 세트에 각 품목의 이름, 판매 수량, 단가가 주어집니다.
#   각 데이터 세트마다 전체 판매 금액을 계산하여, 소수점 둘째 자리까지 반올림한 후
#   "$" 기호와 함께 출력하는 문제입니다.
#
# 해결 방법:
#   첫 번째 입력 줄에 테스트 케이스의 개수 n이 주어집니다.
#   각 테스트 케이스마다 첫 줄에 항목의 개수 x를 입력받고,
#   다음 x개의 줄에 걸쳐 품목 이름, 수량, 단가가 주어집니다.
#   각 항목에 대해 (수량 × 단가)를 계산해 합산하고,
#   최종적으로 "$"와 함께 소수점 둘째 자리까지 출력합니다.
#
# 시간/공간 복잡도 : O(n*x)

n = int(input())
for _ in range(n):
    x = int(input())
    total = 0.0
    for _ in range(x):
        data = input().split()
        # data[0]: 품목 이름, data[1]: 수량, data[2]: 단가
        total += int(data[1]) * float(data[2])
    print(f"${total:.2f}")