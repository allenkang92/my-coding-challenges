# 문제 링크 : https://www.acmicpc.net/problem/9086
# 간단한 문제 설명 : 
#   문자열의 첫 글자와 마지막 글자를 출력하는 프로그램
#   입력은 대문자로만 이루어진 문자열
#
# 해결 방법 설명 : 
#   - T개의 테스트 케이스를 입력받음
#   - 각 케이스마다:
#     - 문자열 입력받기
#     - 첫 글자([0])와 마지막 글자([-1]) 연결하여 출력
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(T) - T개의 테스트 케이스
#   - 공간 복잡도: O(N) - 각 문자열의 길이

T = int(input())

for _ in range(T):
    letter = input()
    print(letter[0] + letter[-1])