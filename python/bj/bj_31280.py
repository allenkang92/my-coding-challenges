# 문제 링크 (주석) : https://www.acmicpc.net/problem/31280
# 간단한 문제 설명 : 마술사가 모자에서 각 색상의 쥐를 최소한 한 마리씩 꺼내기 위해 필요한 최소한의 쥐 수 계산
# 해결 방법 설명 : 최악의 경우를 고려하여 필요한 쥐 수 계산
# 시간/공간 복잡도 : O(1)

# 입력 처리
a, b, c, d = map(int, input().split())

# 최소한의 쥐 수 계산
# 최악의 경우: 한 색상의 쥐를 모두 꺼내고, 나머지 색상의 쥐를 한 마리씩 꺼냄
min_mice = (a + b + c + d) - min(a, b, c, d) + 1

# 결과 출력
print(min_mice)