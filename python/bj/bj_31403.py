# 문제 링크 (주석) : https://www.acmicpc.net/problem/31403
# 문제 설명 :
#   정수 A, B, C가 주어질 때, 두 가지 방식으로 A+B-C를 계산하여 출력합니다.
#   1) 숫자로서 계산하면, A+B-C 입니다.
#   2) 문자열로서 계산하면, A와 B를 연결한 문자열에서 C를 뺀 결과를 구합니다.
#
#   예를 들어, A=3, B=4, C=5인 경우,
#       - 숫자로서: 3+4-5 = 2
#       - 문자열로서: "3"+"4" = "34"이고, 34-5 = 29가 됩니다.
#
# 해결 방법 설명:
#   첫 번째 줄에는 A+B-C를 정수 연산으로 계산하여 출력하고,
#   두 번째 줄에는 str(A)와 str(B)를 연결한 후, 이를 정수로 변환하여 C를 뺀 값을 출력합니다.
#
# 시간/공간 복잡도 : O(1)

A = int(input())
B = int(input())
C = int(input())

# 숫자로 계산한 결과
print(A + B - C)
# 문자열로 계산한 결과: A와 B를 연결한 후, C를 뺀 값
print(int(str(A) + str(B)) - C)