# 문제 링크 : https://www.acmicpc.net/problem/1271
# 간단한 문제 설명 : 주어진 돈(n)을 생명체 수(m)로 나누어 분배하는 문제입니다.
# 해결 방법 설명 : 1. n을 m으로 나눈 몫으로 각 생명체가 받는 돈을 계산
#                2. n을 m으로 나눈 나머지로 남는 돈을 계산
# 시간/공간 복잡도 : O(1) (단순 산술 연산)

n, m = map(int, input().split())  # 돈의 액수와 생명체 수 입력

print(n // m)  # 각 생명체가 받는 돈 (몫)
print(n % m)   # 남는 돈 (나머지)