# 문제 링크 : https://www.acmicpc.net/problem/26546
# 문제 설명:
#   문자열에서 특정 구간의 부분 문자열을 제거하는 프로그램을 작성합니다.
#   입력으로 문자열과 두 정수 i, j가 주어지며, 
#   i는 제거할 부분 문자열의 시작 인덱스, j는 제거할 부분 문자열의 종료 인덱스(실제로는 j-1까지)가 됩니다.
#   따라서 제거할 부분 문자열의 길이는 j - i 입니다.
#   0 ≤ i ≤ j ≤ 문자열의 길이를 만족한다고 가정합니다.
#
# 출력:
#   주어진 문자열에서 인덱스 i부터 j-1까지의 부분 문자열을 제거한 결과를 출력합니다.
#
# 해결 방법:
#   문자열 s의 앞부분 s[:i]와 뒷부분 s[j:]를 이어붙여 출력하면 됩니다.
#
# 시간/공간 복잡도 : O(n)

n = int(input())
for _ in range(n):
    s, i, j = input().split()
    # i와 j를 정수로 변환하여, s에서 인덱스 i 이전과 인덱스 j 이후의 부분을 이어붙입니다.
    print(s[:int(i)] + s[int(j):])