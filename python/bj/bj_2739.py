# 문제 링크 (주석) : https://www.acmicpc.net/problem/2739
# 간단한 문제 설명 : 
#   N을 입력받은 뒤, 구구단 N단을 출력하는 프로그램을 작성한다.
#   N은 1보다 크거나 같고, 9보다 작거나 같다.
#
# 해결 방법 설명 : 
#   - N을 입력받아 정수로 변환한다.
#   - 1부터 9까지 반복하며 N과 곱한 결과를 출력한다.
#   - 출력 형식: "N * i = result" 형태로 맞춘다.
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 9번의 고정된 반복
#   - 공간 복잡도: O(1) - 상수 공간만 사용

N = int(input())

for i in range(1, 9+1):
    print(N, "*", i, "=", N * i)