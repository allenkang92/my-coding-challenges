# 문제 링크 (주석) : https://www.acmicpc.net/problem/11654
# 간단한 문제 설명 : 
#   알파벳 소문자, 대문자, 숫자 0-9 중 하나가 주어졌을 때
#   주어진 글자의 아스키 코드값을 출력하는 프로그램
#
# 해결 방법 설명 : 
#   - input()으로 한 글자를 입력받음
#   - ord() 함수를 사용하여 아스키 코드값으로 변환
#   - 변환된 값을 출력
#
# 시간/공간 복잡도 : 
#   - 시간 복잡도: O(1) - 단일 문자 처리
#   - 공간 복잡도: O(1) - 상수 공간만 사용

inputs = input()
print(ord(inputs))