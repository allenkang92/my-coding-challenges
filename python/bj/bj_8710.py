# 문제 링크 : https://www.acmicpc.net/problem/8710
# 문제 설명:
#   Kozik(코직)는 농구선수가 되기 위해서 트레이너가 요구하는 키(w) 에 도달해야 한다.
#   현재 코직의 키는 k이며, 농구선수가 되기 위해서는 최소 w의 키가 필요하다.
#   코직은 다소 무리한 방법으로 머리에 충격을 줘서 혹이 생기게 하는데,
#   한 번 충격을 줄 때마다 혹이 m cm 만큼 자란다.
#   코직이 목표 키에 도달하기 위해 최소 몇 번의 충격이 필요한지 구하는 문제입니다.
#
# 해결 방법 설명:
#   코직이 부족한 키의 양은 (w - k)입니다.
#   한 번 충격을 줬을 때 m cm 만큼 증가하므로, 필요한 충격의 횟수는 (w - k) / m 입니다.
#   만약 (w - k)가 m으로 나누어 떨어지지 않으면 한 번 더 충격을 줘야 하므로, 올림 처리를 해줍니다.
#
# 시간/공간 복잡도 : O(1)

k, w, m = map(int, input().split(" "))

if (w - k) % m == 0:
    print((w - k) // m)
else:
    print(((w - k) // m) + 1)