# 문제 링크 : https://www.acmicpc.net/problem/29807
# 문제 설명:
#   김한양의 학번은 다음과 같은 공식으로 계산된다.
#
#   1. 국어와 영어 점수를 비교한다.
#      - 만약 국어 점수가 영어 점수보다 높으면, (국어 점수 - 영어 점수)에 인문관 건물 번호 508을 곱한다.
#      - 그렇지 않으면, (영어 점수 - 국어 점수)에 국제관 건물 번호 108을 곱한다.
#
#   2. 수학과 탐구 점수를 비교한다.
#      - 만약 수학 점수가 탐구 점수보다 높으면, (수학 점수 - 탐구 점수)에 제1공학관 건물 번호 212를 곱한다.
#      - 그렇지 않으면, (탐구 점수 - 수학 점수)에 ITBT관 건물 번호 305를 곱한다.
#
#   3. 제2외국어 과목을 응시했다면 (응시하지 않았으면 점수는 0점으로 간주),
#      제2외국어 점수에 행원파크 건물 번호 707을 곱한다.
#
#   위의 세 값을 모두 더한 뒤, 한양대학교의 우편번호 04763(=4763)을 곱하면 학번이 나온다.
#
#   단, 과목은 순서대로 국어, 수학, 영어, 탐구, 제2외국어 순으로 주어지며,
#   “앞의 과목을 응시하지 않으면 뒤의 과목을 응시할 수 없다”는 원칙에 따라, 
#   응시하지 않은 과목은 0점으로 취급한다.
#
# 입력:
#   첫 번째 줄에 김한양이 응시한 과목 수 T (1 ≤ T ≤ 5)가 주어진다.
#   둘째 줄에 T개의 정수가 공백으로 구분되어 주어지며, 
#   순서대로 국어, 수학, 영어, 탐구, (제2외국어)가 주어진다.
#   응시하지 않은 과목은 입력에 주어지지 않으며 0점으로 간주한다.
#
# 출력:
#   김한양의 학번을 출력한다.
#
# 해결 방법:
#   - 입력받은 점수 리스트의 길이를 5로 맞춰 부족한 과목은 0으로 채운다.
#   - 국어(0)와 영어(2)를 비교하여 해당하는 계산값을 구한다.
#   - 수학(1)과 탐구(3)를 비교하여 해당하는 계산값을 구한다.
#   - 제2외국어(4)는 있으면 해당 점수에 707을 곱한다.
#   - 세 값을 합한 후 4763을 곱한 값을 출력한다.
#
# 시간/공간 복잡도: O(1)

T = int(input())
scores = list(map(int, input().split()))

# 만약 응시한 과목이 5개 미만이면 나머지를 0으로 채운다.
while len(scores) < 5:
    scores.append(0)

# scores[0]: 국어, scores[1]: 수학, scores[2]: 영어, scores[3]: 탐구, scores[4]: 제2외국어

# 1. 국어와 영어 점수 비교
if scores[0] > scores[2]:
    val1 = (scores[0] - scores[2]) * 508
else:
    val1 = (scores[2] - scores[0]) * 108

# 2. 수학과 탐구 점수 비교
if scores[1] > scores[3]:
    val2 = (scores[1] - scores[3]) * 212
else:
    val2 = (scores[3] - scores[1]) * 305

# 3. 제2외국어 (응시했다면)
val3 = scores[4] * 707

# 최종 학번 계산
student_id = (val1 + val2 + val3) * 4763

print(student_id)