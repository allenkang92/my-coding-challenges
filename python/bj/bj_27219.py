# 문제 링크 : https://www.acmicpc.net/problem/27219
# 간단한 문제 설명 :
#   무인도에 홀로 있는 로빈슨 크루소는 매일 자신의 오두막 벽에 날짜를 표시합니다.
#   매일 하루가 지날 때마다 "I"라는 표시를 하나 남기고, 5일이 지나면 이전 4개의 "I"를 지워 "V"로 바꿉니다.
#   예를 들어, n번째 날에 도달했을 때, 벽에는 (n // 5)개의 "V"와 (n % 5)개의 "I"가 표시됩니다.
#
# 해결 방법 설명 :
#   n을 5로 나눈 몫만큼 "V", 나머지만큼 "I"를 출력하면 됩니다.
#
# 시간/공간 복잡도 : O(1)

n = int(input())
print("V" * (n // 5) + "I" * (n % 5))