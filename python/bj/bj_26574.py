# 문제 링크 (주석) : https://www.acmicpc.net/problem/26574
# 간단한 문제 설명 : 
#   n개의 숫자를 입력받아 각 숫자를 공백을 두고 두 번 출력하는 문제입니다.
#
# 해결 방법 설명 :            
#   1. 먼저 출력할 숫자의 개수 n을 입력받습니다.
#   2. n번 반복하며 숫자를 입력받습니다.
#   3. 각 숫자를 공백을 사이에 두고 두 번 출력합니다.
#
# 시간/공간 복잡도 : O(n)

n = int(input())  # 숫자의 개수 입력

for _ in range(n):
    num = int(input())  # 숫자 입력
    print(num, num)     # 같은 숫자를 공백을 두고 두 번 출력