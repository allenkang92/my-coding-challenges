# 문제 링크 (주석) : https://www.acmicpc.net/problem/24086
# 간단한 문제 설명 :
#   JOI 君 (조이 군)의 1년 전 키가 A cm, 현재 키가 B cm로 주어질 때,
#   이 1년 동안 키가 얼마나 자랐는지를 cm 단위(단위는 출력하지 않음)로 구하는 문제입니다.
#
# 해결 방법 설명 :
#   키의 증가는 현재 키 B에서 1년 전 키 A를 빼면 구할 수 있습니다.
#   문제의 조건에 따라 A < B이므로 B - A의 결과는 양수입니다.
#
# 시간/공간 복잡도 : O(1)

A = int(input())  # JOI 君의 1년 전 키 (cm)
B = int(input())  # JOI 君의 현재 키 (cm)

# 키의 상승분을 계산: 현재 키에서 1년 전 키를 뺍니다.
print(abs(B - A))