# 문제 링크 : https://www.acmicpc.net/problem/24072
# 문제 설명:
#   ビーバーのビ太郎는 오늘부터 A일 후 오전에 실가에 도착하고,
#   오늘부터 B일 후 오전에 실가를 떠난다.
#   반면, ビバ子는 오늘부터 C일 후 오후에 방문한다.
#   ビバ子가 ビ太郎를 만날 수 있으면 1, 그렇지 않으면 0을 출력한다.
#
# 해결 방법 설명:
#   ビ太郎가 집에 있는 시간은 A일 후(오전)부터 B일 후(오전)까지이다.
#   ビバ子는 C일 후 오후에 방문하므로,
#   만약 A ≤ C < B이면 ビ太郎가 집에 있을 때 방문하는 것이므로 1을 출력하고,
#   그렇지 않으면 0을 출력한다.
#
# 시간/공간 복잡도 : O(1)

A, B, C = map(int, input().split())

if A <= C < B:
    print(1)
else:
    print(0)