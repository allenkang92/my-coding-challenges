# 문제 링크 : https://www.acmicpc.net/problem/2442
# 간단한 문제 설명 : 첫째 줄부터 N번째 줄까지 별을 가운데 정렬하여 출력하는 문제
# 해결 방법 설명 : 각 줄마다 앞에 공백을 N-i-1개 출력하고, 별을 2*i+1개 출력
# 시간/공간 복잡도 : O(N²), N은 입력된 줄 수

# 입력: 줄 수 N
N = int(input())

# 각 줄마다 별 출력
for i in range(N):
    # 앞 공백: N-i-1개 (각 줄마다 하나씩 줄어듦)
    # 별: 2*i+1개 (첫 줄은 1개, 이후 줄마다 2개씩 증가)
    print(" " * (N - i - 1) + "*" * (2 * i + 1))