# 문제 링크 (주석) : https://www.acmicpc.net/problem/18691
# 문제 설명:
#   Pokemon Go의 Buddy 업데이트에서는 포켓몬 그룹에 따라 일정 킬로미터를 걸으면 캔디가 지급된다.
#   그룹 1은 1km마다, 그룹 2는 3km마다, 그룹 3은 5km마다 캔디를 주며, 포켓몬 진화를 위해서는 특정 개수의 캔디가 필요하다.
#   이 문제에서는 포켓몬 그룹 G, 현재 가지고 있는 캔디 C, 그리고 진화에 필요한 캔디 수 E가 주어질 때,
#   포켓몬 진화를 위해 걸어야 하는 최소 킬로미터 수를 구하는 프로그램을 작성한다.
#
# 해결 방법:
#   우선, 진화에 필요한 추가 캔디 수 (E - C)를 구한다.
#   만약 C가 E 이상이면 이미 충분하므로 0km를 출력한다.
#   부족한 경우, 포켓몬 그룹에 따라 1km, 3km, 5km마다 캔디를 주므로,
#   추가 캔디 수와 km당 캔디 지급량에 따라 총 걸어야 할 km를 계산한다.
#
# 시간/공간 복잡도 : O(T), T는 테스트 케이스 수

T = int(input())
for _ in range(T):
    G, C, E = map(int, input().split(" "))
    # 현재 캔디가 진화에 필요한 캔디보다 많거나 같으면 걷는 거리는 0km
    if C >= E:
        print(0)
    else:
        # 부족한 캔디 수
        needed = E - C
        if G == 1:
            km = needed * 1
        elif G == 2:
            km = needed * 3
        elif G == 3:
            km = needed * 5
        print(km)