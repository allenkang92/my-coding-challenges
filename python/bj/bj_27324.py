# 문제 링크 (주석) : https://www.acmicpc.net/problem/27324
# 간단한 문제 설명 : 
#   2자리 정수 N이 주어질 때, 십의 자리와 일의 자리가 같으면 1을, 다르면 0을 출력하는 문제입니다.
#
# 해결 방법 설명 :            
#   1. 두 자리 정수를 문자열로 입력받습니다.
#   2. 입력받은 문자열의 첫 번째 문자(십의 자리)와 두 번째 문자(일의 자리)를 비교합니다.
#   3. 두 숫자가 같으면 1을, 다르면 0을 출력합니다.
#
# 시간/공간 복잡도 : O(1)

N = input()  # 두 자리 정수를 문자열로 입력받음 (10 ≦ N ≦ 99)

if N[0] == N[1]:  # 십의 자리와 일의 자리가 같은지 비교
    print(1)      # 같으면 1 출력
else:
    print(0)      # 다르면 0 출력