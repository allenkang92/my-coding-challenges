# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6033
# 간단한 문제 설명 : 문자 1개를 입력받아 그 다음 문자를 출력합니다.
# 해결 방법 설명 : 1. input() 함수로 문자 1개를 입력받습니다.
#                2. ord() 함수로 문자를 아스키 코드 값으로 변환합니다.
#                3. 아스키 코드 값에 1을 더합니다.
#                4. chr() 함수로 아스키 코드 값을 다시 문자로 변환합니다.
#                5. 결과를 출력합니다.
# 시간/공간 복잡도 : O(1) (단순 입출력 및 형변환 연산)

a = input()
p = chr(ord(a)+1)
print(p)