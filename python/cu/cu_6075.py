# 문제 링크 : https://codeup.kr/problem.php?id=6075 
# 간단한 문제 설명 :
#   정수(0~100)를 입력받아 0부터 그 수까지 순서대로 출력하는 문제입니다.
#
# 해결 방법 설명 :
#   for문과 range()를 사용하여 0부터 입력받은 수까지 순회하며 출력합니다.
#   range(A+1)은 0부터 A까지의 숫자 시퀀스를 생성합니다.
#            
# 시간/공간 복잡도 : O(n) (n은 입력된 정수의 크기)

A = int(input())  # 정수 입력 (0~100)

for i in range(A+1):  # 0부터 A까지 순회
    print(i)          # 현재 숫자 출력