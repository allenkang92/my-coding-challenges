# 문제 링크 : https://codeup.kr/problem.php?id=6011
# 간단한 문제 설명 : 실수를 입력받아 실수형으로 변환하여 출력합니다.
# 해결 방법 설명 : 1. input() 함수로 문자열 입력 받기
#                2. float() 함수로 문자열을 실수로 변환
#                3. print() 함수로 변환된 실수 출력
# 시간/공간 복잡도 : O(1) (단순 입출력 및 형변환 연산)

f = float(input())  # 문자열을 입력받아 실수로 변환
print(f)            # 실수 출력