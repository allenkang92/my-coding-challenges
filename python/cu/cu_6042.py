# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6042
# 간단한 문제 설명 : 실수 1개를 입력받아 소수점 이하 두 번째 자리까지 반올림하여 출력합니다.
# 해결 방법 설명 : 1. input() 함수로 실수를 입력받습니다.
#                2. float() 함수로 입력값을 실수로 변환합니다.
#                3. round() 함수를 사용하여 소수점 이하 두 번째 자리까지 반올림합니다.
# 시간/공간 복잡도 : O(1) (단순 입출력 및 반올림 연산)

f = float(input())

print(round(f, 2))