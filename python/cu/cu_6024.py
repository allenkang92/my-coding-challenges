# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6024
# 간단한 문제 설명 : 알파벳과 숫자로 이루어진 두 단어를 입력받아 붙여서 출력합니다.
# 해결 방법 설명 : 1. input().split()으로 두 단어를 분리합니다.
#                2. + 연산자를 사용하여 두 단어를 연결합니다.
#                3. 연결된 단어를 출력합니다.
# 시간/공간 복잡도 : O(1) (단순 입출력 및 문자열 연결 연산)

word1, word2 = input().split()

print(word1 + word2)