# 문제 링크 : https://codeup.kr/problem.php?id=6079
# 간단한 문제 설명 : 
#   1부터 차례대로 더했을 때, 입력된 수보다 같거나 커지게 만드는 정수를 찾는 문제입니다.
#
# 해결 방법 설명 :            
#   1. 변수 sum에 1부터 차례대로 숫자를 더해갑니다.
#   2. sum이 입력된 수 n보다 커지거나 같아지면 현재 더한 숫자를 출력합니다.
#
# 시간/공간 복잡도 : O(n) (n은 입력된 정수의 크기)

n = int(input())  # 정수 입력 (0~1000)
sum = 0  # 합계를 저장할 변수
i = 0    # 현재 더할 숫자

while True:
    i += 1        # 더할 숫자를 1 증가
    sum += i      # 합계에 현재 숫자를 더함
    if sum >= n:  # 합계가 입력된 수 이상이면
        print(i)  # 현재 숫자를 출력하고
        break     # 반복 종료