#                2. input() 함수로 두 번째 실수를 입력받습니다.
#                3. float() 함수를 사용하여 각 입력을 실수로 변환합니다.
#                4. 두 실수의 합을 계산하여 출력합니다.
# 시간/공간 복잡도 : O(1) (단순 입출력 및 산술 연산)

