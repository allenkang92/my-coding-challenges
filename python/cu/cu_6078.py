# 문제 링크 : https://codeup.kr/problem.php?id=6078
# 간단한 문제 설명 :
#   영문 소문자 'q'가 입력될 때까지 입력된 문자를 계속 출력하는 문제입니다.
#
# 해결 방법 설명 :
#   1. while True로 무한 루프를 만듭니다.
#   2. 문자를 입력받아 출력합니다.
#   3. 입력된 문자가 'q'이면 해당 문자를 출력한 후 루프를 종료합니다.
#            
# 시간/공간 복잡도 : O(n) (n은 입력되는 문자의 개수)

while True:
    n = input()        # 문자 입력
    print(n)           # 입력된 문자 출력
    if n == 'q':      # 'q'가 입력되면
        break          # 반복 종료