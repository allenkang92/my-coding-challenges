# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6071
# 간단한 문제 설명 :
#   임의의 정수들이 계속 입력될 때, 0이 입력되기 전까지 입력된 정수를 출력하는 문제입니다.
#   (입력되는 정수는 -2147483648 ~ +2147483647 범위)
#
# 해결 방법 설명 :
#   while True를 사용하여 무한 루프를 만들고, 입력값이 0이면 break로 종료합니다.
#   0이 아닌 경우에는 입력된 정수를 출력합니다.
#
# 시간/공간 복잡도 : O(n) (n은 입력되는 정수의 개수)

while True:
    n = int(input())
    if n == 0:
        break
    print(n)