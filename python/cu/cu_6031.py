# 문제 링크 : https://codeup.kr/problem.php?id=6031
# 간단한 문제 설명 : 10진수 정수를 입력받아 유니코드 문자로 출력합니다.
# 해결 방법 설명 : 1. input() 함수로 10진수 정수를 입력받습니다.
#                2. int() 함수로 입력값을 정수로 변환합니다.
#                3. chr() 함수로 해당 정수의 유니코드 문자를 구합니다.
#                4. 유니코드 문자를 출력합니다.
# 시간/공간 복잡도 : O(1) (단순 입출력 및 형변환 연산)

c = int(input())
print(chr(c))