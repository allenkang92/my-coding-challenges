# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6014
# 간단한 문제 설명 : 실수 하나를 입력받아 세 번 출력합니다.
# 해결 방법 설명 : 1. input() 함수로 문자열 입력 받기
#                2. float()로 실수로 변환
#                3. print()로 세 번 출력
# 시간/공간 복잡도 : O(1) (단순 입출력 및 형변환 연산)

f = float(input())  # 실수 입력 받기

print(f)  # 첫 번째 출력
print(f)  # 두 번째 출력
print(f)  # 세 번째 출력