# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6016
# 간단한 문제 설명 : 공백으로 구분된 두 문자를 입력받아 순서를 바꿔 출력합니다.
# 해결 방법 설명 : 1. input().split()으로 공백으로 구분된 문자열을 리스트로 변환
#                2. 두 변수에 각각 할당
#                3. print()의 쉼표 구분자를 이용해 순서를 바꿔 출력
# 시간/공간 복잡도 : O(1) (단순 입출력 연산)

ch1, ch2 = input().split()  # 공백으로 구분된 두 문자 입력

print(ch2, ch1)  # 순서를 바꿔 출력 (쉼표로 구분하면 자동으로 공백이 추가됨)