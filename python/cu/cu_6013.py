# 간단한 문제 설명 : 두 개의 문자를 줄을 바꿔 입력받고 순서를 바꿔 출력합니다.
# 해결 방법 설명 : 1. input() 함수로 첫 번째 문자 입력 받기
#                2. input() 함수로 두 번째 문자 입력 받기
#                3. print()로 역순으로 출력
# 시간/공간 복잡도 : O(1) (단순 입출력 연산)

ch1 = input()  # 첫 번째 문자 입력
ch2 = input()  # 두 번째 문자 입력

print(ch2)     # 두 번째 문자 먼저 출력
print(ch1)     # 첫 번째 문자 나중에 출력