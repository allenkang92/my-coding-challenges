# 문제 링크 : https://codeup.kr/problem.php?id=6077
# 간단한 문제 설명 :
#   정수(1~100)를 입력받아 1부터 그 수까지 짝수의 합을 구하는 문제입니다.
#
# 해결 방법 설명 :
#   1. for문을 사용하여 1부터 입력받은 수까지 순회합니다.
#   2. 현재 숫자가 짝수인 경우(i % 2 == 0)에만 합계에 더합니다.
#   3. 최종 합계를 출력합니다.
#            
# 시간/공간 복잡도 : O(n) (n은 입력된 정수의 크기)

n = int(input())  # 정수 입력 (1~100)
s = 0  # 짝수의 합을 저장할 변수

for i in range(1, n+1):  # 1부터 n까지 순회
    if i % 2 == 0:      # 현재 숫자가 짝수인 경우
        s += i          # 합계에 더함

print(s)  # 최종 합계 출력