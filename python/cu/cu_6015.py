# 문제 링크 : https://codeup.kr/problem.php?id=6015
# 간단한 문제 설명 : 공백으로 구분된 두 정수를 입력받아 줄을 바꿔 출력합니다.
# 해결 방법 설명 : 1. input().split()으로 공백으로 구분된 문자열을 리스트로 변환
#                2. map(int, ...)로 문자열을 정수로 변환
#                3. 두 변수에 각각 할당하고 순서대로 출력
# 시간/공간 복잡도 : O(1) (단순 입출력 및 형변환 연산)

int1, int2 = map(int, input().split())  # 공백으로 구분된 두 정수 입력

print(int1)  # 첫 번째 정수 출력
print(int2)  # 두 번째 정수 출력