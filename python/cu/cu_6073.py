# 문제 링크 : https://codeup.kr/problem.php?id=6073
# 간단한 문제 설명 :
#   정수(1~100)를 입력받아 카운트다운을 출력하되,
#   입력된 수보다 1 작은 수부터 시작하여 0까지 출력하는 문제입니다.
#
# 해결 방법 설명 :
#   while문을 사용하여 입력받은 수에서 1을 뺀 값부터 0까지 출력합니다.
#   n이 0이 되면 출력 후 반복을 중단합니다.
#
# 시간/공간 복잡도 : O(n) (n은 입력된 정수의 크기)

n = int(input())

while True: 
    n -= 1    # 먼저 1을 감소시키고
    print(n)  # 그 값을 출력
    if n == 0:  # n이 0이 되면 반복 중단
        break