# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6074
# 간단한 문제 설명 : 
#   영문 소문자(a~z) 1개가 입력되었을 때, a부터 입력된 문자까지 순서대로 출력하는 문제입니다.
#
# 해결 방법 설명 :
#   1. ord()를 사용하여 입력된 문자와 'a'를 ASCII 코드값으로 변환합니다.
#   2. while문을 사용하여 'a'부터 입력된 문자까지 순회합니다.
#   3. chr()를 사용하여 ASCII 코드값을 다시 문자로 변환하여 출력합니다.
#   4. end=' '를 사용하여 출력 시 줄바꿈 대신 공백을 넣습니다.
#            
# 시간/공간 복잡도 : O(n) (n은 입력된 문자와 'a' 사이의 문자 개수)

c = ord(input())  # 입력된 문자를 ASCII 코드값으로 변환
t = ord('a')      # 'a'의 ASCII 코드값
while t <= c:     # 'a'부터 입력된 문자까지 반복
    print(chr(t), end = ' ')  # ASCII 코드값을 문자로 변환하여 출력, 줄바꿈 대신 공백 사용
    t += 1        # 다음 문자로 이동