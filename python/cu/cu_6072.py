# 문제 링크 (주석) : https://codeup.kr/problem.php?id=6072
# 간단한 문제 설명 :
#   정수(1~100)를 입력받아 카운트다운을 출력하는 문제입니다.
#
# 해결 방법 설명 :
#   while문을 사용하여 입력받은 수부터 1씩 감소시키며 출력합니다.
#   n이 0이 되면 반복을 중단합니다.
#
# 시간/공간 복잡도 : O(n) (n은 입력된 정수의 크기)

n = int(input())

while n > 0:  # n이 0보다 클 때까지 반복
    print(n)  # 현재 n 값 출력
    n -= 1    # n을 1씩 감소